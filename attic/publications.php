<h2>Data Release Publications</h2>

<p>Our <a href="science/dr_publications.php">Data Release Publications</a> describe what data
each of our data releases contain, how the data were collected, and technical
details of the project. The most current public data release of the SDSS-III is
<a href="dr9/">Data Release 9</a>, released in July 2012. Data Release 9 is
described in the <a href="dr9/">DR9 site</a>, and in the
<a href="science/dr9.pdf">DR9 paper (PDF)</a>.</p>

<h2>Technical Publications</h2>

<p>Our <a href="science/technical_publications.php">Technical Publications</a> describe various aspects of the technical operation of the Sloan Digital Sky Survey.
