<p> SDSS-III will release the full imaging data set taken by the SDSS
imaging camera. These pages describe the
nature of the DR8 imaging data and how to use it. In particular:</p>

<ul>
<li> <a href="dr8/imaging/imaging_basics.php">Basics</a> describes
