<p>Building on the Legacy of the
<a href="http://www.sdss.org">Sloan Digital Sky Survey (SDSS)</a>
and SDSS-II, the SDSS-III Collaboration is working to map the
Milky Way, search for extrasolar planets, and solve the
mystery of dark energy.
</p>

<p>
SDSS-III's newest release is <a href="dr12/">Data Release 12</a> (DR12).
DR12 contains the final data from SDSS-III, including the first spectra of the
<a href="surveys/marvels.php">Multi-object APO Radial Velocity Exoplanet Large-area Survey (MARVELS)</a>,
additional data from the
<a href="surveys/apogee.php">APO Galactic Evolution Experiment (APOGEE)</a>,
as well as additional sky coverage and better galaxy parameter estimates from
<a href="surveys/boss.php">BOSS</a>.
</p>

<p>
All of the SDSS-III's images, spectra, and measurements are available to anyone online.
You can <a href="http://skyserver.sdss.org/public/en/tools/chart/navi.aspx">browse
through sky images</a>, look up <a href="http://skyserver.sdss.org/public/en/tools/quicklook/quickobj.aspx">data
for individual objects</a>, or <a href="http://skyserver.sdss.org/public/en/tools/search/">search for objects</a>
anywhere using any criteria.
</p>

<p>
Data releases are cumulative, so all previously-released images, spectra, and
measurements are still available in DR12. To enable direct comparison with prior
studies, we continue to host all prior data releases. The SDSS-III's prior releases,
<a href="dr10/">Data Release 10</a>, <a href="dr9/">Data Release 9</a> and <a href="dr8/">Data Release 8</a>, are available
from this site. Older data releases are available from the original SDSS website,
<a href="http://classic.sdss.org/">classic.sdss.org</a>.
</p>


<h2>SDSS-III Surveys</h2>
<p>
SDSS-III began to collect data in 2008, and will continue until 2014, using
the <a href="instruments/telescope.php">Sloan Foundation 2.5-meter Telescope</a>
at Apache Point Observatory in New Mexico. SDSS-III consists of four surveys,
each focused on a different scientific theme. Click on one of the images below for more
information about each survey.
</p>
