<p>Combining SEGUE-1 and 2 reveals the complex kinematic and chemical substructure of
the Galactic halo and disks, providing essential clues to the assembly
and enrichment history of the Galaxy. In particular, the outer halo
is expected to be dominated by late-time accretion events.
SEGUE can help constrain existing models for the formation of the stellar halo
and inform the next generation of high resolution simulations of Galaxy
formation. In addition, SEGUE-1 and SEGUE-2 help uncover rare,
chemically primitive stars that are fossils of the earliest
generations of cosmic star formation. There have already been many different <a
href="science/segue_publications.php">science results</a> from SEGUE
data and many more to come. </p>

