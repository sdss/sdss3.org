<p> The primary goal of SEGUE-1 is a kinematic and stellar population
study of the high-latitude thick disk and halo of the Milky Way.  The
halo is sampled sparsely with a series of seven deg<sup>2</sup> tiles
both in the SEGUE-1 imaging stripes and the main Legacy survey area,
with centers separated by roughly 10 deg.  Each such tile is sampled
with two pointings, split at r=17.8, approximately the median target
magnitude. </p>

<p> Half of the targets on each line of sight are devoted to sampling
across various main sequence types, up to and including the main
sequence turnoff. These are largely selected for spectroscopic follow
up based on straightforward color-color and color-magnitude cuts. To
this sample we add metal-poor main sequence turnoff stars selected by
their blue <i>ugr</i> colors. These targets tend to be at a
heliocentric distance of 10 kpc for [Fe/H] = -1.54.  To reach to
greater distances, SEGUE-1 samples field blue horizontal branch (BHB)
stars, which extends to distances of approximately 40 kpc. Also
reaching this distance is the sample of halo red giants. </p>

<p> The spectroscopic selection also includes smaller categories of
rare but interesting objects.  These include cool white dwarfs, which
can be used to date the age of the Galactic disk, and high proper
motion targets from the SUPERBLINK catalog, which have uncovered some
of the most extreme M subdwarfs known and aided in the calibration of
their metallicity scale using common proper motion pairs.</p>

