 <h2>SEGUE-2: Mapping the Outer Milky Way</h2> 

<p>Much of what we know about the stellar halo comes from
kinematically-selected stars in the solar neighborhood. In any
cosmological scenario we expect the stellar halo to form inside out,
with the outer halo increasingly dominated by recent
accretion. SEGUE-2 capitalizes on the wide field and high throughput
of SDSS instruments to focus on the more distant halo with the goal of
understanding the growth of our stellar halo over time. Specific
questions we hope can be addressed with the SEGUE-2 data include the
fraction of the halo formed by the accretion of dwarf galaxies
vs. <i>in situ</i> star formation, the accretion history of the halo
through time, and whether the chemical abundance distribution of the
more recently accreted, dynamically younger halo resembles the
surviving dwarf galaxies, such that the differences between these
dwarf galaxies and the halo observed in the solar neighborhood result from 
different accretion times. In addition, finding more halo stars is the most 
likely way to increase the number of tracers in the Galactic gravitational 
potential at large distances in order to better determine the total mass of the 
Galaxy.</p> 

<p>To probe the extremes of the halo stellar population distribution, SEGUE-2
specifically targets blue horizontal branch stars, K-giants, and M-giants.
Many important and rare halo tracers are impossible to survey <i>in situ</i>. Thus,
those three stellar categories are complemented
by a collection of main sequence turn off stars that cover a broad enough color
range to include metallicities up to solar and is neither biased in metallicity or
kinematics. </p>

<p>Additional local samples are used by SEGUE-2 to supplement the distant halo survey.
Old, cool white dwarfs can be assigned ages from models in order to date Galactic field
populations, as had been done with ultra-cool white dwarfs found in SDSS and SEGUE-1. Extreme
and ultra-cool subdwarfs explore the low-mass end of the halo mass function and
are sufficiently local to have good 3D kinematics. The cool subdwarfs
and the extremely metal-poor star sample in particular will also be useful
for studies of chemical evolution, nucleosynthesis, and stellar atmospheres.
Finally, SEGUE-2 investigates halo high velocity stars and hypervelocity stars.
</p>

<p>In selecting distant and rare objects, SEGUE-2 benefits from the
experience of SEGUE-1 in developing optimized and efficient selection
techniques. The SEGUE-2 selection methodology uses the most up to date
color and proper motion cuts with SDSS DR7 photometry and astrometry
to target particular spectral types. Additionally, the magnitude
limits for SEGUE-2 use SEGUE-1 as a test case to achieve the optimal
balance between survey depth, sky coverage, and data quality. Whereas
SEGUE-1 matched shorter exposures of bright targets with long
exposures of faint targets, SEGUE-2 obtains only deep pointings, as it
focuses on the far reaches of our Galaxy. </p>


