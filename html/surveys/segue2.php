<h2 id="SEGUE-1">SEGUE-1: Exploring Milky Way Structure</h2>

<p>The SEGUE-2 survey builds off of the work of SEGUE-1. For a thorough
overview of SEGUE-1, see <a
href="http://adsabs.harvard.edu/abs/2009AJ....137.4377Y">Yanny et
al. 2009</a>. To learn more about the differences between SEGUE-1 and
SEGUE-2, please go to the <a
href="dr10/tutorials/segue_getting_started.php#segue1_vs_segue2">Getting
Started</a> page.</p>

<p>SEGUE-1 was designed to explore the Milky Way structure; formation
history; kinematics; dynamical evolution; chemical evolution; and dark
matter distribution. The images and spectra obtained by SEGUE-1
allowed astronomers to map the positions and velocities of hundreds of
thousands of stars, from faint, relatively near-by (within about 100
pc or roughly 300 light-years) ancient stellar embers known as white
dwarfs to bright stellar giants located in the outer reaches of the
stellar halo, more than 100,000 light-years away. Encoded within the
spectral data are the composition and temperature of these stars,
vital clues for determining the age and origin of different
populations of stars within the Galaxy. </p>

