<p>The original Sloan Extension for Galactic Understanding
and Exploration (SEGUE-1) obtained spectra of nearly
230,000 unique stars over a range of spectral types to investigate Milky Way 
structure. Building on this success,
  SEGUE-2 spectroscopically observed around 119,000 unique stars,
focusing on the <i>in situ</i> stellar halo of the Galaxy,
from distances of 10 to 60 kpc. </p>

