<p>With well-characterized sensitivity and a broad range of target star properties,
MARVELS will provide a critical dataset for testing theoretical models of the formation,
migration, and dynamical evolution of giant planet systems. It will have unique sensitivity
to rare systems such as extreme eccentricity planets or objects in the "brown dwarf desert."</p>


