<p>The Multi-object APO Radial Velocity Exoplanet Large-area Survey (MARVELS) will monitor the radial velocities of 11,000 bright stars, with the precision and cadence needed to detect gas giant planets that have orbital periods ranging from several hours to two years.</p>

