<p>The APO Galactic Evolution Experiment (APOGEE) is using high-resolution, high
signal-to-noise infrared spectroscopy to penetrate the dust that obscures significant
fractions of the disk and bulge of our
Galaxy. APOGEE will survey over 100,000 red giant stars across the full range of the
Galactic bulge, bar, disk, and halo.  Precise radial velocities and detailed chemical
abundance "fingerprinting" will provide unprecedented insights into the dynamical
structure and chemical history of the Galaxy.  APOGEE will also contribute to unraveling problems
in fundamental astrophysics, and complement other major surveys such as Kepler and CoRoT.</p>

<p>The SEGUE-2 and APOGEE datasets will play a central role in near-field cosmology
tests of galaxy formation and the small-scale distribution of dark matter.</p>

