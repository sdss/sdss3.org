<p>The original Sloan Digital Sky Survey and SDSS-II have a rich scientific legacy.
<a href="http://www.sdss.org/signature.html">Discoveries</a> made with SDSS data have included:</p>

<ul>
<li>Detection of the baryon acoustic peak in the clustering of galaxies</li>
<li>Mapping of streams of stars left from galaxy mergers in the Milky Way, as well as the discovery of many new dwarf companion galaxies of the Milky Way </li>
<li>The most distant quasars known</li>
<li>Cool brown dwarfs, the largest sample of spectroscopically confirmed white dwarfs (by far) and many other classes of unusual stars</li>
<li>Detecting extreme kinematical and chemical populations of the Galactic bulge</li>
</ul>

<p>The <a href="http://www.amnh.org">American Museum of Natural History</a> hosts a nice
<a href="http://www.amnh.org/explore/science-bulletins/(watch)/astro/documentaries/sloan-digital-sky-survey-mapping-the-universe">video overview of SDSS science</a>
accessible to the general public.</p>

<p>The SDSS data have been made public in a series of data releases; the most recent is the
<a href="dr10/">Tenth Data Release</a>, which includes the complete
SDSS/SDSS-II data.  It includes links to the flat files in the <a href="http://data.sdss3.org">Science
Archive Server</a> and to the catalogs and databases in the <a href="http://skyserver.sdss3.org">Catalog Archive Server</a>.</p>

<p>SDSS-III builds on the legacy of the SDSS and SDSS-II to generate high-quality
scientific data and to make important new discoveries. SDSS-III has been designed to
maximize understanding of three scientific themes:</p>

<ul>
<li>Dark energy and cosmological parameters</li>
<li>The structure, dynamics and chemical evolution of the Milky Way</li>
<li>The architecture of planetary systems</li>
</ul>

<p>For a detailed description of the SDSS-III's science program, see the
<a href="collaboration/description.pdf">Project Description</a>, available as a PDF document.
Also there is a recent <a href="http://adsabs.harvard.edu/abs/2011AJ....142...72E">overview paper</a> now published in
<em>Astronomical Journal</em> (see also <a href="http://arxiv.org/abs/1101.1529">the arXiv preprint</a>).
</p>

