<h2 id="overview">BOSS Overview Paper</h2>

<p>The motivation for BOSS and an overview of target selection, survey design, and
observing strategy are described in Dawson et al. 2013,
<a href="http://adsabs.harvard.edu/abs/2013AJ....145...10D">The Baryon
Oscillation Spectroscopic Survey of SDSS-III</a>, published in the January 2013 issue of
<em>Astronomical Journal</em>. A <a href="science/boss.pdf">preprint-style
PDF of the paper</a> is available on this site.</p>

<h2 id="clustering">DR12 Galaxy Clustering Science Results</h2>

   <p> Here we list the series of papers that comprise the final data analysis of the BOSS galaxy survey, with links to measurements, covariance matrices, likelihoods, and figures. For quick reference, all files can be found on the SDSS SAS <a href="https://data.sdss.org/sas/dr12/boss/papers/clustering/">here</a>. These papers represent analysis of the "combined" sample of DR12 galaxies. Previous DR12 analyses (as well as the anlayses of data releases 9 through 11) split the BOSS galaxy sample into two main target classifications of LOWZ and CMASS.</p>

