<h3 id="clustering">DR9 Galaxy Clustering Science results presented in BOSS publications</h3>

   <p>Below we list measurements, covariance matrices and analysis tools
   that have been presented and utilized in BOSS DR9 galaxy clustering papers.
   The covariance matrices for all measurements are calculated from the 600
   mock DR9 CMASS realizations, created using PTHalos, described in
   <a href="http://adsabs.harvard.edu/abs/2013MNRAS.428.1036M">Manera et al. (2013)</a>.
   Please cite this paper if the errors are used in any analysis.</p>

