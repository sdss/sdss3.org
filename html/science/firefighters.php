<p>
With an observatory at Apache Point, New Mexico, the
<a href="http://www.sdss3.org">Sloan Digital Sky Survey </a>
(SDSS)
relies on the work and bravery of firefighters
who protect the forests of the American Southwest.
Like astronomers across the nation, we were
<a href="http://blog.sdss3.org/2013/07/02/our-deepest-sympathies-and-thanks-to-the-wildfire-fighters-in-the-american-southwest/">
deeply saddened
</a>
by the loss of 19 members of the
<a href="http://www.cityofprescott.net/services/fire/hotshots/">
Granite Mountain Hotshots</a>
from Prescott, Arizona in June 2013 during the Yarnell Hill fire.</p>

<p>
SDSS scientists have used the survey's imaging data to discover
large numbers of asteroids in the solar system, and with
the discovery comes the right to name the asteroids once their
orbits have been precisely determined.
The SDSS has named an asteroid for each of these 19 firefighters.
Each asteroid is identified by a number and a name.
Below we provide links to the basic data on each of these asteroids
from a database maintained by NASA's Jet Propulsion Laboratory.
We also link to a site that shows the current position of the asteroid
relative to the planets of the inner solar system (Mercury, Venus,
Earth, and Mars), which is
maintained by the University of West Bohemia
in the Czech Republic.
</p>

<p>
Each of these asteroids carries a citation:
"... was one of the 19 elite Prescott's
Granite Mountain Hotshot firefighters who lost their lives battling
a blaze on a ridge in Yarnell, AZ.  Crews such as the Hotshots are
connected to astronomy through their brave efforts to protect forests and
the observatories within them from fire threats."</p>

