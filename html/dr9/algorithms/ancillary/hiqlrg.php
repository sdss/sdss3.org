<h2>Description</h2>

<p>Two plates (3615 and 3647) are being observed repeatedly throughout the survey, several
times per year. The goal of these repeat observations is twofold: to test the
reproducibility of spectral parameters extracted by the spectral pipelines, and to create
deep spectra of a subset of objects in the main BOSS sample.</p>

<p>Spectroscopic observations of targets in this ancillary target program will
produce accumulated exposure times of more than 20 hours each for all targets,
including 96 LOZ and 503 CMASS LRGs at &alpha; = 37 degrees on Stripe 82.
The plates contain identical spectroscopic targets, but are drilled for different
airmasses.</p>

<p>The survey acquired 16.5 hours of total spectroscopic exposure for all targets on these
two plates between December 1, 2009 and July 15, 2011. Observations will continue in each
of the three remaining years of the survey. The final observations will produce high
signal-to-noise spectra, which will enable detailed spectral population studies of
intermediate-redshift LRGs. The will data also provide a platform for tests of the
consistency in spectroscopic parameter extraction (e.g.,
<a href="http://adsabs.harvard.edu/abs/2012AJ....143...90S">Shu et al. 2012</a>,
Thomas et al., <i>in prep.</i>), and of systematic errors in the models of continuum fitting
in the Lyman-alpha forest region  of the quasar sample (e.g.; Lee et al., <i>in prep.</i>).</p>


