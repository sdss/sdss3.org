<li id="cas">
<h3>What interfaces are available to SDSS-III catalog data?&nbsp;<a href="dr10/help/faq.php#Top"><small>Top</small></a></h3>

<p><a href="http://skyserver.sdss3.org">SkyServer</a> (also known as the Catalog
Archive Server [CAS]) is the website that hosts SDSS-III's catalog data. The
<a href="http://dr10.sdss3.org">Science Archive Server</a> (SAS) serves flat files
