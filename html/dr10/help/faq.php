<li id="findobj">
<h3>How can I see if DR10 has an image or spectrum of my favorite sky
object?&nbsp;<a href="dr10/help/faq.php#Top"><small>Top</small></a></h3>

<p>Go to the SkyServer <a href="http://skyserver.sdss3.org/dr10/en/tools/chart/navi.aspx">
Navigate</a> tool. Enter the name of your object in the "Name" box, then press Enter.
The RA/dec coordinates of the object will appear in the boxes below. Then, click Get Image.</p>

<p>If a sky image appears in the main Navigate frame, then DR10 has an image for your object.</p>

<p>If there is an image for your object, you can also check to see whether DR10 includes a
spectrum. Under the RA/Dec boxes, find the <span class="term">Drawing options</span>.
Click the checkbox next to <span class="term">Objects with spectra</span>. If a red square
appears around your object, then your object has a measured spectrum in DR10.</p>

<p>Whether or not your object has a spectrum in DR10, the object's magnitude data will appear
in the right-hand pane. Click <span class="term">Quick Look</span> to go see additional
basic data about that object. Click <span class="term">Explore</span> for more detailed data.</p>
</li>

<li id="searchtools">
<h3>How can I search for data?&nbsp;<a href="dr10/help/faq.php#Top"><small>Top</small></a></h3>
<p>When you search for data in the SDSS, you are going through the SDSS
database and looking for objects that match criteria you
choose. For simple searches of photometric data, use the
<a href="http://skyserver.sdss3.org/dr10/en/tools/search/iqs.aspx">Imaging Query Form</a>.
For simple searches of spectroscopic data, use the
<a href="http://skyserver.sdss3.org/dr10/en/tools/search/sqs.aspx">Spectroscopic Query Form</a>.
For more complex searches,  use Structured Query Language (SQL). See SkyServer's
guide on <a href="http://skyserver.sdss3.org/dr10/en/help/howto/search/">Searching for Data</a>
to learn more about SQL. To see thumbnail results of objects that meet your
criteria, use the <a href="http://skyserver.sdss3.org/dr10/en/tools/chart/list.aspx">Image List</a> tool.</p>

<p>For large, complex queries that will take a long time to run, use
<a href="http://skyserver.sdss3.org/casjobs/">CasJobs</a>,
the SDSS's batch query interface.</p>

<p>Many other search tools are available. See the <a href="dr10/data_access/">DR10 Data Access</a>
pages for more options.</p>

<p>An important note if you are using Excel with the
<a href="http://skyserver.sdss3.org/dr10/en/proj">SkyServer educational projects</a>: SDSS
object IDs are so long that they get cut off in Excel, and show up with 000 as the last
three digits. This means you won't be able to find your objects anymore! To get around
this problem, see this <a
href="http://skyserver.sdss3.org/dr10/en/help/howto/graph/caveat.aspx">workaround</a>. </p>
</li>


<li id="crossmatch">
<h3>How can I match a list of objects to see what data are available for them in DR10?&nbsp;<a href="dr10/help/faq.php#Top"><small>Top</small></a></h3>

<p>
If you have a fairly small list of objects to match - a few hundred or so - use the
<a href="http://skyserver.sdss3.org/dr10/en/tools/crossid/">SkyServer Cross-ID</a>
tool. Paste your list of objects, or upload a file containing data with the last two
columns as (ra, dec) in decimal degrees. Click Submit. The next page will show only those
objects that appear in the SDSS, with SDSS Object IDs that
link to the <a href="http://skyserver.sdss3.org/dr10/en/tools/explore/obj.aspx">Explore</a> tool.</p>

<p>To see a thumbnail SDSS image of each matching object, use the <a href="http://skyserver.sdss3.org/dr10/en/tools/chart/list.aspx">Image List</a> tool. Enter
your list in the textbox on the upper left and click "Get Image". Click on one of the thumbnails to go to that position in the
<a href="http://skyserver.sdss3.org/dr10/en/tools/chart/navi.aspx">Navigate</a> tool, or on one of the object names to go to that object's
<a href="http://skyserver.sdss3.org/dr10/en/tools/explore/obj.aspx">Explore</a> tool entry.</p>

<p>
For longer lists, use the Neighbors Search feature of
<a href="http://skyserver.sdss3.org/casjobs/">CasJobs</a>. See the
<a href="http://skyserver.sdss3.org/casjobs/faq.aspx#crossid">CasJobs FAQ entry</a> on "How can I cross-identify
(find matching objects in SDSS) for my list of sources for which I have RA,decs?" for more information.
</p>
</li>


