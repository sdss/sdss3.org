<h2>About the SDSS</h2>

<ol>

<li id="whatissdss">
<h3>What is the Sloan Digital Sky Survey?&nbsp;<a href="dr10/help/faq.php#Top"><small>Top</small></a></h3>

<p>The Sloan Digital Sky Survey is an ongoing astronomy project that has been working since 1998
to make a detailed map of the universe, including our Milky Way galaxy. Since 2008, the survey
has been in operating its third phase. Results and from the first two phases are available at
<a href="http://www.sdss.org">www.sdss.org</a>. Results and data from the current (third) phase are
available on this website, <a href="index.php">www.sdss3.org</a>.</p>
</li>

<li id="whythethree">
<h3>What is SDSS-III? Why the "three"? <a href="dr10/help/faq.php#Top"><small>Top</small></a></h3>

<p>The Sloan Digital Sky Survey has been funded to operate in three phases. Phase one, from
1998 to 2005, was the original <a href="http://www.sdss.org">Sloan Digital Sky Survey</a>.</p>

<p>Phase two, from 2005 to 2008, was the Sloan Digital Sky Survey II (SDSS-II), which consisted
of three interrelated surveys. The <a href="http://www.sdss.org/legacy/index.html">SDSS
Legacy Survey</a> completed observations on the full coverage area of the SDSS as originally
planned. The SDSS-II <a href="http://www.sdss.org/supernova/aboutsupernova.html">Supernova
Survey</a> scanned the same area of sky each clear night for three years, and found
hundreds of type Ia supernovae. The SDSS-II <a href="http://www.sdss.org/segue/">SEGUE</a>
survey (Sloan Extension for Galactic Understanding and Exploration) mapped the
structure and composition of the Milky way.</p>

<p>Phase three, the Sloan Digital Sky Survey III (SDSS-III), is the current phase of operations.
This website, accessible from the <a href="index.php">SDSS-III home page</a>, describes
SDSS-III, its <a href="surveys/">four component surveys</a>, and its
<a href="science/">science results</a>. This site also includes data collected by the
SDSS-III. The latest data is <a href="dr10/">Data Release 10</a>, made public on July 30, 2013.</p>
</li>

<li id="data">
<h3>What data does SDSS have? What can SDSS tell me about the sky? <a href="dr10/help/faq.php#Top"><small>Top</small></a></h3>

<p>The most recent SDSS data release, the ninth overall, is <a href="dr10/">SDSS Data Release
10 (DR10)</a>, made public on July 31, 2012. It includes most data from prior data releases; so to get the most recent,
up-to-date data for anywhere in the sky, use <a href="dr10/">Data Release 10</a>.</p>

<p>Data Release 10 includes <a href="dr10/imaging/">imaging data</a> for about one-third of the
night sky. The imaging data include images from the camera on the SDSS's 2.5-meter telescope as
preview images (JPGs) and as FITS files. The data also include
catalogs of detected objects, with parameters
measured from imaging, including positions and magnitudes. DR10 also includes
<a href="dr10/spectro/">spectroscopic data</a> for more than three million
stars, galaxies, and quasars, Spectra are available as preview images (PNGs) and FITS files.
Spectroscopic catalog data include object types and redshifts.</p>

<p>All DR10 data are available from this site. See <a href="dr10/data_access/">DR10 Data Access</a>
to learn how to view and download DR10 data.</p>

<p>Prior SDSS-III data releases are also available on this site.
<a href="dr9/">Data Release 9 (DR9)</a> was made public on July 31, 2012.
<a href="dr8/">Data Release 8 (DR8)</a> was made public on January 11, 2011.</p>

<p>The final data release for SDSS-I and SDSS-II is <a href="http://www.sdss.org/dr7">Data
Release 7</a>.</p>

</li>

<li id="learn">
<h3>I am a teacher. How can I use the data in my classes?&nbsp;<a href="dr10/help/faq.php#Top"><small>Top</small></a></h3>

<p>The <a href="education/">SDSS-III Education</a> page describes what educational
resources are available from the SDSS-III project.</p>

<p>The SDSS-III SkyServer website includes a variety of
<a href="http://skyserver.sdss3.org/dr10/en/proj/">educational projects</a> that
use SDSS data to teach topics in astronomy and other sciences,
using guided and open inquiry. With our Projects, you and your students can
learn about spectra and
colors of stars, galaxy types, the history of the universe, and much more.</p>

<p>You are welcome to use and adapt any of our projects in your classes, free of charge. For more
information on what you can do with SkyServer in the classroom, see our
<a href="http://skyserver.sdss3.org/dr10/en/proj/teach.aspx">Teacher FAQ</a>.</p>
</li>

<li id="brightstars">
<h3>Why doesn't the SDSS have data for well-known visible stars (Sirius,
Vega, etc.)?&nbsp;<a href="dr10/help/faq.php#Top"><small>Top</small></a></h3>
<p>The SDSS has a very sensitive <a href="instruments/camera.php">camera</a>.
Stars that you can see with your unaided eyes are a too bright for the
SDSS's camera; they are saturated, and the light from them spills all
over the detector. The SDSS still gets an image of
those stars (for example, here is an image of <a href="http://skyserver.sdss3.org/dr10/en/tools/chart/navi.aspx?ra=116.2997&amp;dec=28&amp;scale=10">Pollux</a> from SkyServer),
but the image processing software is unable to handle these images,
and there are no entries in the SDSS catalog.</p>
</li>

<li id="stargalaxy">
<h3>Why are some bright stars classified as galaxies?&nbsp;<a href="dr10/help/faq.php#Top"><small>Top</small></a></h3>
<p>The SDSS distinguishes between stars and galaxies based on their shapes: single points of light are stars, and
fuzzy patches of light are galaxies. Some stars are bright enough that
their light saturates the camera, and thus the light spills over the image, so
to the SDSS's camera, they look like fuzzy disks instead of single points of light. Their appearance fools the
SDSS's software into classifying them as galaxies.</p>
</li>



</ol>

