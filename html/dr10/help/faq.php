<p>There are multiple interfaces to the SDSS-III catalog data, including:</p>

<ul>
<li><a href="http://skyserver.sdss3.org/">SkyServer</a>: an
interface to the Catalog Archive Server designed for astronomers,
students and the general public</li>
<li><a href="http://skserver.sdss3.org/casjobs/">CasJobs</a>: a batch (asynchronous) system for querying the database and storing results, designed to allow astronomers to search
SDSS-III catalog data flexibly and efficiently</li>
<li><a
href="http://skyserver.sdss3.org/dr10/en/help/download/sqlcl">sqlcl</a>:
a command-line Python interface to SkyServer</li>
<li><a href="http://www.astro.princeton.edu/~rhl/skyserver/">Emacs
buffer</a>: an Emacs interface to SkyServer for running queries</li>
<li><a href="http://dr10.sdss3.org">Science Archive Server (SAS)</a>: simple queries for
retrieval of individual spectra and field binary FITS images, plus
direct download of flat files</li>
</ul>
</li>

