<li id="scienceprimary">
<h3>What is the difference between <code>SpecObj</code> and <code>SpecObjAll</code>? What
do <code>sciencePrimary</code> and <code>bossPrimary</code> mean?
&nbsp;<a href="dr10/help/faq.php#Top"><small>Top</small></a></h3>

<p>The <code>SpecObjAll</code> table contains <span class="term">all</span> spectroscopic
objects, even duplicate spectra of the same object. Thus, we have
created the <code>SpecObj</code> view, which contains data for <span class="term">only</span>
those fibers defined as <span class="term">sciencePrimary</span> - essentially, the best
spectrum that the SDSS has obtained for that object. That is, <code>specObj</code>
contains no duplicate observations.</p>

<p>Similar to <code>sciencePrimary</code>, another parameter called <code>bossPrimary</code>
flags the best spectrum that BOSS has obtained for an object. Exact definitions of
<code>sciencePrimary</code> and <code>bossPrimary</code> can be found in the
<a href="dr10/spectro/catalogs.php">spectroscopic catalogs</a> documentation.</p>

</li>

<li id="photoobjtag">
<h3>What are the differences between <code>PhotoObj</code>, <code>PhotoTag</code>,
and <code>PhotoObjAll</code>?&nbsp;<a href="dr10/help/faq.php#Top"><small>Top</small></a></h3>

<p><code>PhotoObjAll</code> is a table that contains
<span class="term">all</span> of the measured photometric quantities for
<span class="term">all</span> of the imaging detections for every object.
Because we measure hundreds of parameters for each of 1.2 billion detections, this is a
very large table, and queries can take very long to run. Also,
<code>PhotoObjAll</code> includes duplicate and "special" detections such as <a
href="dr10/help/glossary.php#parent">parent</a> or <a
href="dr10/help/glossary.php#bright">bright</a> detections, which are normally not of
interest to science users.</p>

<p>In an effort to speed up catalog data queries, we have created
<code>PhotoTag</code>, a view (virtual or logical table) of PhotoObjAll
with only a subset of the photometric parameters that are
most often requested. Queries on this "thin" version of PhotoObjAll run
much faster usually than queries on the larger table. If you have a
query that uses and returns only values stored in <code>PhotoTag</code>,
then you should definitely use <code>PhotoTag</code>.</p>

<p>In addition, we have created <code>PhotoObj</code>, a view of
<code>PhotoObjAll</code> that contains only those objects that are <a
href="dr10/help/glossary.php#surveyprimary">survey primary</a> or <a
href="dr10/help/glossary.php#surveysecondary">survey secondary</a>.
<code>PhotoObj</code> excludes special objects such as
<a href="dr10/help/glossary.php#parent">parent</a> or <a
href="dr10/help/glossary.php#bright">bright</a> detections. Because
<code>PhotoObj</code> contains the same parameters as
<code>PhotoObjAll</code> but effectively contains fewer objects,
any queries for survey primary or survey secondary objects will run
faster on <code>PhotoObj</code>.</p>


<p>Finally, there is <code>PhotoPrimary</code>, a view of
<code>PhotoObjAll</code> that only includes
<a href="dr10/help/glossary.php#surveyprimary">survey primary</a> detections;
if you are not interested in duplicate detections at all, this is the
view to use.</p>

To summarize, the <code>PhotoTag</code> view is a vertical partition of
<code>PhotoObjAll</code> (fewer columns), and <code>PhotoObj</code>,
<code>PhotoPrimary</code> and <code>PhotoSecondary</code> are horizontal
partitions of <code>PhotoObjAll</code> (fewer rows).

<p>Given the above, you should consider:</p>

<ol>
<li>Querying from <code>PhotoTag</code> if it contains all the parameters you are using
to search and all the parameters you want returned</li>
<li>Querying from <code>PhotoObj</code> otherwise, UNLESS you are interested in data
for objects that are <span class="term">neither PRIMARY nor SECONDARY</span>. In that
case, you will need to use the full <code>PhotoObjAll</code> table.</li>
<li>Importantly, the "shorthand" quantities <span class="term">u,g,r,i,z</span> do
<span class="term">not</span> exist in <code>PhotoTag</code> (because we want to
keep the view as thin as possible). Instead, you must use the
<span class="term">modelMag_[ugriz]</span> columns. HOWEVER, in
<code>PhotoObjAll</code> and its views, only the <span
class="term">u,g,r,i,z</span> are indexed, and NOT the ModelMags! This
difference in indexed columns is to save space (since indexes on a huge
table like <code>PhotoObjAll</code> are expensive), not to confuse you!</li>
</ol>

<p>Because <code>PhotoTag</code> has many fewer parameters, larger portions of it
can be cached, improving performance. We have found that for almost
all queries that contain parameters fully in <code>PhotoTag</code>, it is
faster. </p>

</li>

<li id="specphotoall">

<h3>How do I get photometry for spectroscopic objects? What are the <code>SpecPhotoAll</code>
table and <code>SpecPhoto</code> view?
&nbsp;<a href="dr10/help/faq.php#Top"><small>Top</small></a></h3>

<p>The <em>SpecPhotoAll</em> table is a precomputed join between the
<em>PhotoObjAll</em> and
<em>SpecObjAll</em> tables. It includes the most requested parameters
from these two tables. Because the join has already been performed, it
can be faster than computing the join on the fly. The
<code>SpecPhoto</code> view contains the same parameters as <code>SpecPhotoAll</code>,
but only for objects that are <a href="dr10/help/glossary.php#surveyprimary">survey
primary</a> or <a href="dr10/help/glossary.php#surveysecondary">survey secondary</a>.</p>

</li>

<li id="numprec">
<h3>Why do some values (such as <code>z</code> and <code>zErr</code> in <code>SpecObj</code>)
have different numerical precisions?&nbsp;<a href="dr10/help/faq.php#Top"><small>Top</small></a></h3>
<p>Internally, these numbers are stored to their full precision as they come out
of the spectroscopic pipeline. When you perform a query, they have some
default string format applied that cuts them to what you see.</p>

<p>But you can use SQL's <code>
<a href="http://msdn.microsoft.com/en-us/library/aa259237%28v=sql.80%29.aspx">str()<img src="images/offsite.png" alt=" (offsite)" /></a> </code>
function to change the string format to whatever you like.</p>

<p>To get z to 6 decimals, for example, change your query to search for
<code>select str(z,8,6) as z</code> instead of just <code>z</code>, and analogously
for <code>zErr</code>. This applies the function <code>str()</code>
to the values in column <code>z</code> and returns the result with column label
<code>z</code> (without the "as", the result of a function has no column label). The function
<code>str(col,length,dec)</code> takes the numerical value in <code>col</code> and formats
it as a string of length <i>length</i> and with <i>dec</i> significant digits. In other words,
<code>str(z,8,6)</code> is the SQL equivalent to the C function
<code>printf("%8.6f",z)</code>. The <code>str()</code> function rounds the
result to the number of decimals you request.  </p>
</li>

<li id="spectype2">
<h3>What is the difference between <em>class</em> and <em>type</em> for spectroscopic objects, and which one should I use?&nbsp;<a href="dr10/help/faq.php#Top"><small>Top</small></a></h3>

<p>For any object in DR10, the <code>type</code> parameter in <code>SpecObj</code> and other
tables was set when that object was <span class="term">targeted</span>
for spectroscopy (i.e., it is based on its morphology in the imaging data).
The <code>class</code> parameter is set by the spectroscopic pipeline
<span class="term">after</span> the spectrum is observed (i.e., it is
based on features in the spectrum).</p>

<p>In nearly all cases, you should believe the classification in <code>class</code>, since it
tells you what the measured spectrum actually was. The <code>type</code> field tells you about
why it was targeted. (However, there is more information about the object's target selection
history in the target bits; see the <a href="dr10/algorithms/bitmasks.php">target bitmasks</a>
pages for more information.</p>
</li>

