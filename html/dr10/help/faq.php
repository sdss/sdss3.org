SDSS images is to select some objects as targets for follow-up spectroscopy. The SDSS
then collects spectra later. Most spectra were collected using the original
<a href="instruments/segue2_spectrograph.php">SDSS-I/-II spectrograph</a>. DR10
includes data collected using the
<a href="instruments/boss_spectrograph.php">BOSS spectrograph</a> and for the
