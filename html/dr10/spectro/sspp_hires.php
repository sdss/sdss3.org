<p>The figure below shows comparisons of <var>T</var><sub>eff</sub> (left panels), 
log <i>g</i> (middle panels), and [Fe/H] (right panels) of the DR9 SSPP with 
the temperature from the IRFM, and surface gravity and metallicity from the 
new high-resolution analysis. The symbols &mu; and &sigma; are the mean and 
standard deviation from a Gaussian fit to the sample. 'Adop' is the final 
adopted value in the SSPP; 'Hi-res' refers to the high-resolution analysis. As 
was the case for DR8, the DR9 SSPP gravity estimate still over-estimates 
log <i>g</i> by up to 1.0 dex for very cool giants. Note that there are only 
107 stars available for the temperature comparison, as only those 
stars have J, H, and K photometry available.</p>

<p>
Considering the typical error for measuring the surface gravity in the high-resolution 
analysis to be around 0.2 dex, the uncertainty in log <i>g</i> of the SSPP is 
expected to be less than 0.35 dex from a quadratic subtraction, (0.4<sup>2</sup>-0.2<sup>2</sup>)<sup>1/2</sup>. 
For the metallicity, the typical error of 0.1 dex in the high-resolution analysis yields 
less than 0.22 dex in the uncertainty of the SSPP-derived [Fe/H] from a 
quadratic subtraction, (0.24<sup>2</sup>-0.1<sup>2</sup>)<sup>1/2</sup>. These 
inferred errors in the SSPP log <i>g</i> and [Fe/H] from the high-resolution 
calibration stars are mostly for the stars with high S/N (&ge;30).
</p>

