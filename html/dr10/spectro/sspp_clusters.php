<h2>Comparison of [Fe/H] of cluster members between DR9 SSPP and literature</h2>

<p>The following plots compare [Fe/H] of some globular
and open cluster member stars derived by the DR9 version of the SSPP.
The solid line indicates the literature value from high-resolution spectroscopic
analysis, while the dashed line is ours. It shows how well the
overall metallicity from the DR9 SSPP agrees with the literature,
without any significant trend along g-r or S/N from very metal-poor
cluster (M92 or M15) to very metal-rich open cluster (NGC 6791). This
proves the very reliable estimate of [Fe/H] from the DR9 SSPP.
</p>

