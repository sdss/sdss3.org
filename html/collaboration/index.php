<p>The SDSS-III collaboration contains scientists from <a
href="collaboration/institutions.php">many
international member institutions</a> (including full members, associate
members and members that are part of internal participation
groups). The <a href="collaboration/membership.pdf">membership
document</a> (PDF) gives details of how a new institute can join the
collaboration and the different levels of collaboration
membership. Please use the <a href="collaboration/boiler-plate.php">
official acknowledgement</a> when acknowledging the SDSS-III
collaboration, including member institutions and funding agencies.</p>

<p>The SDSS-III Collaboration Council (CoCo) is the most active forum
for the discussion of SDSS-III collaboration issues and has one
representative from all the full and associate institutional members
(full/associate membership is defined in the
<a href="collaboration/poo3.pdf"> PoO-III</a> or the <a
href="collaboration/membership.pdf">membership document</a>). The CoCo is
chaired by the SDSS-III Spokesperson, who is also
responsible for promoting the productivity of the collaboration and
science output of the surveys. More information on the CoCo membership
and recent activity can be found on the <a
href="https://trac.sdss3.org/wiki/CoCo/">internal CoCo wiki-page</a>
(password protected).</p>


