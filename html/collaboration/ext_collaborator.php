<h2> Introduction </h2>

<p>The SDSS-III Principles of Operation (PoO) states that data rights are
in the hands of scientists at an SDSS-III institution ("Participants")
who have PI status or whose primary responsibility is the construction
of the SDSS-III, and of individuals at other institutions granted such
Participant status. The <a href="collaboration/poo3.pdf"> PoO-III </a> describes the
principles under which SDSS-III data may be used collaboratively by
non-Participants prior to public release. The purpose of this document
is to define efficient procedures for implementing this section of the
PoO-III. The procedures are slightly different for "inside"
collaborators, who are at SDSS-III institutions, and for "outside"
collaborators, who are not. </p>

<p>Examples of the kinds of collaborations covered by this policy
include:</p>

<ul>

<li>follow-up observations of interesting SDSS objects with larger
telescopes or at other wavelengths; </li>

<li>use of SDSS data in generating
observing proposals for space observatories, in collaboration with
some non-SDSS astronomers; </li>

<li>searching the SDSS catalogues for objects
with the color signature of some particular type of object (e.g., T
Tauri stars), in collaboration with a non-SDSS scientist who has
devised an efficient search algorithm; </li>

<li>comparison of the SDSS
luminosity functions for galaxies with data on faint galaxies observed
at Keck; </li>

<li>comparison of SDSS data to data from other surveys (e.g.,
2MASS) in a particular area of the sky; </li>

<li>comparison of SDSS star counts with a new model for the Galaxy.</li>

</ul>

<h2> Inside Collaborations </h2>

<p>In practice, much of the scientific analysis of the SDSS data will
be done by students and postdocs at SDSS institutions. These "inside"
collaborators access the data through individuals at their institution
with data rights. These individuals (i.e., the graduate and
postdoctoral advisors) are responsible for ensuring that their
collaborators read and agree to the PoO-III and <a
href="collaboration/publication.php"> publication policy </a>, and they are
responsible for ensuring that these policies are followed and that the
data are not released outside the collaboration. They and their
institutions are also responsible for providing the necessary research
support: computing, page charges, trips to scientific meetings, and so
forth.</p>

<p>Because collaborative work with students and postdocs will be the
norm rather than the exception, no special permission is required to
initiate such collaborations. However, as with all SDSS-III
investigations, a team contact should provide notice to the
collaboration upon the initiation of any project that is expected to
lead ultimately to a publication, as described in the PoO and the
Publication Policy. Such notification is especially important in the
case of Ph.D. theses, so that those concerned are aware of any
potential conflicts with other projects. The notification of
initiation of an investigation should be submitted by an individual
with data rights and should list all of the student, postdoctoral, and
other collaborators. By submitting the notification, the individual
with data rights implicitly accepts responsibility for ensuring that
SDSS-III policies are followed by the non-Participants who are
collaborating on the project.</p>

<h2> External Collaborations with those leaving SDSS institutions </h2>

<p> An individual at an SDSS-III institution who is working on projects
involving SDSS-III data and leaves that SDSS-III institution may request
External Collaborator status for those particular projects, so that
he/she may continue working on them after he/she leaves. The request
shall be made to the Chair of the Collaboration Council, and the
decision will be made by the Management Committee in consultation with
the leaders of the projects involved, the relevant working group
chairs, and the CoCo. Normally, External Collaborator status will be
granted provided that the individual has had significant involvement
in the project before he/she leaves. This policy applies to faculty,
postdocs, and graduate students. Individuals with portable data rights
(participant status), of course, need not make such a request.</p>

<h2> Outside Collaborations </h2>

<p>Collaborations involving colleagues not at an SDSS-III institution and
visitors at SDSS-III institutions (hereafter non-SDSS scientists) are
treated differently. These collaborators will again access the data
through Participants with data rights. At the initiation of such
collaborations, the SDSS scientist(s) involved should:</p>

<ol>

<li>Inform the non-SDSS scientist(s) of the SDSS-III rules and guidelines
(the PoO, the Publication Policy, and this Collaboration Policy) and
obtain his/her consent to abide by them. In particular, non-SDSS
scientists should understand and agree that they may not disseminate
the SDSS-III data to which they have access in any way, and that
publications resulting from the collaboration will be subject to the
Publication Policy.</li>

<li>Request comment and input from the relevant working groups (by
e-mail to the relevant working group); <strong> this step is strongly
encouraged.</strong></li>

<li><p>Obtain permission to proceed from the Collaboration Council
(CoCo) by submitting a proposal. In an e-mail message sent to
sdss3-general and to the chair of the CoCo (<a
href="mailto:sdss-coco@jiscmail.ac.uk"> sdss-coco at jiscmail.ac.uk </a>),
the SDSS-III Participant(s) involved should briefly describe the
proposed project, list the names and institutions of the proposed
collaborators, and state their willingness to ensure that the outside
collaboration will proceed according to the SDSS-III rules, policies
and guidelines. Most important, they should indicate what special
resources or expertise the outside collaborators bring to the project
(e.g., telescope access, experience with specific analysis
techniques). This justification for inclusion of external
collaborators should be a labeled, separate paragraph of the proposal
so that it can be clearly evaluated.</p>

<p> Permission can be expected in all cases where the outside
collaborators bring resources or expertise that are not readily
available within the SDSS-III collaboration itself. The guiding
principle in these matters is to allow SDSS-III Participants the first
opportunity to carry out science involving SDSS-III data, and to bring
in outside collaborators only when necessary to get the science done
in the pre-release period. Multiple collaborations pursuing similar
ends are allowed, so the establishment of one outside collaboration
does not preclude another along similar lines.</p>

<p>Since collaborations involving non-participants require permission
from the Collaboration Council, postings should be phrased as
"proposals" rather than as "announcements", to avoid confusion.</p>

<p>Objections to or comments on External Collaboration proposals from
members of the collaboration or from the working group chairs should
be forwarded to the CoCo chair within two weeks of the proposal's
posting on sdss-general. Normally, the CoCo chair has four weeks to
notify proposers of approval or of any objections to the proposed
collaboration, consulting with other CoCo members, working group
chairs, etc., as he/she deems necessary. If the CoCo decides that it
needs more time to consider a given proposal, it may extend the time
frame (within reason) and give notice to the project team of the new
decision date.</p>

<p>Rarely, proposals involving external collaborators will be
time-critical "targets of opportunity" which require faster decision
times. In these cases, the CoCo shall make every effort to respond to
proposals within 10 days. Such time-critical proposals should be
identified as such, and justification for the rapid time frame should
be spelled out. Faster approval will only be given if a compelling
case is made.</p></li>

</ol>

<p>Once a project involving external collaborators is approved, the
participants involved should:</p>

<ol class="i">

<li>Post notice of the proposed project(s) to the Publication Archive,
as per usual.</li>

<li>Post papers resulting from such collaborations to the SDSS-III
collaboration and invite authorship, as for an "internal" SDSS paper,
and be sure the outside collaborators understand in advance that this
will take place.</li>

<li>Send a brief annual progress report to the Collaboration Council
outlining progress made, future plans, and specifying the contribution made by the external collaborators.</li>

</ol>

<p>If a project involving external collaborators is not approved by
CoCo, the project team may appeal the decision to the Management
Committee.</p>

<p>If a project is not approved and moves ahead in any case (e.g.,
follow-up observations are taken), the participants involved will be
deemed to be in violation of this policy only if a publication results
or if a public announcement of the results is made. At that time, the
CoCo chair will inform the Management Committee that those involved
are deemed to be in violation of the PoO-III.</p>

<p>For the case of students and postdocs supervised by SDSS-III
Participants, see the policy on Data Access by Students and Postdocs.</p>



