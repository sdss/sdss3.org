<p>
The SDSS uses a dedicated 2.5 m f/5 modified Ritchey-Chretien
altitude-azimuth telescope located at <a
href="http://www.apo.nmsu.edu/">Apache Point Observatory</a>, in south
east New Mexico (Latitude 32&deg; 46' 49.30" N, Longitude 105&deg; 49'
13.50" W, Elevation 2788m). A 1.08 m secondary mirror and two corrector
lenses result in a 3&deg; distortion-free field of view.  The telescope is
described in detail in a paper by <a href="http://adsabs.harvard.edu/abs/2006AJ....131.2332G">Gunn et al. (2006)</a>.
