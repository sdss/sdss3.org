<p>For more than a decade, the Sloan Digital Sky Survey (SDSS) has been
mapping the stars in our galaxy.  At today's meeting of the
American Astronomical Society, astronomers Judy Cheng and
and Connie Rockosi (University of California, Santa Cruz)
presented new evidence that will help answer long-standing
questions about the history of disk of our galaxy.</p>

<p>These SDSS results come from the Sloan Extension for Galactic
Understanding and Exploration 2 (SEGUE-2) in the SDSS-III project,
which has measured the motions and chemical compositions of more than
118,000 stars.  Some of those stars are in the disk of our galaxy,
which is what we see as the bright band of stars in the night sky that
we call the Milky Way.  Stars in the disk orbit around the center of
the galaxy.  Most of the orbits line up in a flat plane like planets
around the Sun &mdash; but a few orbit to the beat of a different drummer.</p>

<p>"Some disk stars have orbits that take them far above and below the
plane of the Milky Way," explains Connie Rockosi, an astronomer at
University of California Santa Cruz, and principal investigator for the
SEGUE-2 survey.  "We want to understand what kinds of stars
those are, where they came from, and how they got there."</p>

<p>The orbits of these stars make them clearly different from mainstream
Milky Way stars &mdash; but new research shows that their chemical composition
also makes them unique. A team led by Judy Cheng of University of
California Santa Cruz studied the amount of metals in such stars at
different locations in the galaxy.</p>

<p>By making these measurements, Cheng's team was able to look back in
time at how the Milky Way disk grew. The first generation of stars
consisted entirely of hydrogen and helium. Over time, those early
stars turned some of their hydrogen and helium into heavier elements,
like calcium or iron &mdash; and when those stars died, the heavier
elements they produced became part of the next generation of stars. As
new stars were born and the Milky Way disk grew, each generation had
more calcium, iron, and other heavy elements. Thus, we can learn which
parts of our galaxy have seen several generations of stars come and
go, simply by looking at the metal content of stars in that part of
the galaxy.</p>

<p>Cheng's team surveyed the metal content of thousands of stars in the
disk.  Near the plane of the galactic disk &mdash; home of the ordinary
disk stars &mdash; stars closer to the center of the galaxy have higher metal
content than those farther from the galactic center. "That tells us
that the outer disk of our Galaxy has formed fewer generations of
stars than the inner disk &mdash; meaning that the Milky Way disk grew from
the inside out," says Cheng.</p>

<p>But then Cheng studied the "different drummer" stars, those that are
clearly part of the Milky Way disk but show up far above or below the
disk plane. She found that the amount of heavier elements in those stars
doesn't follow the same trend &mdash; everywhere she looked in that part of
the galaxy, stars had low metal content. "The fact that the metal
content of those stars is the same everywhere is a new piece of evidence
that can help us figure out how they got to be so far away from the
plane," Rockosi says.</p>

<p>What we do not yet know is whether these stars were born with such
strange orbits, or whether something happened in the past to put them
on these unique paths. "If these stars were born with these orbits,"
says Cheng, "they were born at the same rate all over the galaxy. If
they were born with regular orbits, then whatever happened to them
must have been very efficient at mixing them up and erasing any
patterns in the metal content, such as the inside-out trend we see in
the plane."  Possible explanations for such efficient mixing
include long-ago collisions between our galaxy and its neighbors, or
the effect of spiral arms sweeping through the disk. Cheng's
observations will help determine whether such major events in the
lives of these stars caused them to wander far from their birthplace.</p>

<p>Disk stars are observed far from the plane in many other galaxies, so
solving the puzzle presented by these stars observed by SDSS will help
us understand a basic part of how spiral galaxies like the Milky Way
form.</p>

