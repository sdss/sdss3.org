<p>The Sloan Digital Sky Survey III (SDSS-III) has released the largest-ever
three-dimensional map of massive galaxies and distant black holes, which will help
astronomers explain the mysterious "dark matter" and "dark energy" that scientists know
makes up 96 percent of the Universe.</p>

<p>Early last year, the SDSS-III released the largest-ever image of the sky. With the
new release of data, SDSS-III has begun to expand this image into a full
three-dimensional map. <a href="dr9/">Data Release 9</a> (DR9), posted online last
week, makes available the first third of the galaxy map that this six-year
project will create.</p>

