<p>
"What really makes me proud of this survey is our commitment to
creating a legacy for the future," said Michael Blanton, a professor
at New York University who led the team that prepared DR9.  "Our goal
is to create a map of the Universe that will be used long after we are
done, by future generations of astronomers, physicists and the general
public."
</p>

<p>Data Release 9 is the latest in a series of data releases stretching
back to 2001. This release includes new data from the ongoing SDSS-III
<a href="surveys/boss.php">Baryon Oscillation Spectroscopic Survey (BOSS)</a>,
which will eventually measure the positions of 1.5 million massive
galaxies over the past six billion years of cosmic time, as well as
160,000 quasars &mdash; giant black holes actively feeding on stars and
gas &mdash; from as long ago as 12 billion years in the past.</p>

<p>
With such a map, scientists can retrace the history of the Universe over the
last six billion years. With that history, they can get better estimates for
how much of the Universe is made up of dark matter - matter that we can't
directly see because it doesn't emit or absorb light - and dark energy, the
even more mysterious force that drives the accelerating expansion of the Universe.
</p>


<p>
"Dark matter and dark energy are two of the greatest mysteries of our time,"
said David Schlegel of Lawrence Berkeley National Laboratory, who led the SDSS-III
effort to map these galaxies and quasars. "We're confident this new map will
guide us, or someone else, in solving these mysteries."
</p>

