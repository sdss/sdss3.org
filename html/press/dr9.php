<p>That map of the Universe is the centerpiece of DR9. It includes new spectra from
540,000 galaxies from when the Universe was half its present age. A spectrum is a
measure of light from a galaxy at different wavelengths; from this information we
can measure the distances to those galaxies. This distance information provides
the third dimension in the DR9 map, providing a more detailed view of the
structure of the Universe than has been measured before.</p>


<p>
Quasars provide further details to the three dimensional map and another way
to measure the distribution of matter in the Universe. Quasars are the brightest
objects in the distant Universe, and their spectra reveal intricate patterns
imprinted by the intergalactic gas and underlying dark matter that lies
between each quasar and the Earth.
</p>


<p>The new data in DR9 are not only helping us understand the distant Universe, but
also our own cosmic backyard, the Milky Way galaxy. DR9 includes better estimates
for the chemical compositions of more than half a million stars in our own galaxy.
"With these better estimates, we can look back at the history of our galaxy," said
Connie Rockosi of the  University of California Santa Cruz, who leads the SDSS-
III's Milky Way study. "This new information can help us tell the story of how our
galaxy formed, and how it came to be the Milky Way that we see today."</p>

<p>
All these new images and spectra contain the promise of new discoveries about our Universe -- but the SDSS-III is only in the middle of its six-year survey, and will release three times as much data by the time it's completed in 2014.
</p>


<p>
"The most fun part of making this data available online is knowing that anyone on the Internet can now access the very same data and search tools that professional astronomers use to make exciting discoveries about our Universe," said Ani Thakar of Johns Hopkins University, a key member of the data team who works hard behind-the-scenes to distribute the terabytes of SDSS-III data to astronomers and the public through these data releases.
</p>

<p>
And DR9 doubtless contains many surprises.
</p>

<p>
"This is science at its collaborative best. SDSS-III scientists work together to address big questions extending from our own galaxy to distant reaches of the Universe -- and then they share all of that data with the world to allow anyone to make the next big discovery," said Michael Wood-Vasey, a professor at the University of Pittsburgh and the Scientific Spokesperson for the SDSS-III collaboration.
</p>

<p>
All the data are available now on the <a href="http://www.sdss3.org/dr9">Data Release 9
website</a>. The new data are being made available to astronomers, as well as students, teachers, and the
public. The <a href="http://skyserver.sdss3.org">SkyServer website</a>  includes <a href="http://skyserver.sdss3.org/public/en/proj/">lesson plans for teachers</a> that use DR9 data to teach astronomy and other topics in science, technology, and math. DR9 data will also feature in a new release of the <a href="http://www.galaxyzoo.org">Galaxy Zoo</a> citizen science project, which will allow online volunteers to contribute to cutting-edge astronomy research.
</p>

