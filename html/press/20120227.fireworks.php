<p>Then came the hard part. The three sub-spectra were never intended for public use, so they were hard to find, and in a format that was, as Badenes says, "unfriendly." Badenes and Maoz worked with Steve Bickerton, an astronomer at Princeton University, to gather and clean the sub-spectra. "We had to process the three sub-spectra separately, but process them in a way that we could directly compare them," says Bickerton. "It wasn't easy."</p>

<p>But persistence paid off, and within a year, the team had a list of more than 4,000 white dwarfs, each of which had two or more high-quality sub-spectra. Some of those four thousand, the team hoped, would show evidence that the star's velocity had changed between the three sub-spectra, indicating that what looked like one star was actually a double white dwarf.</p>

<p>From among the original four thousand, the team found fifteen stars certain to be double white dwarfs&mdash;and "if there were more than 15, we would have seen more than 15," Badenes says. Having found 15 double white dwarfs in the local neighborhood, the team could then use computer simulations to calculate the rate at which double white dwarfs merge. They could then compare the number of merging white dwarfs here to the number of Type Ia supernovae seen in distant galaxies that resemble the Milky Way.</p>

<p>The result? On average, one double white dwarf merger event occurs in the Milky Way about once a century. That number is remarkably close to the rate of Type Ia supernovae we observe in galaxies like our own, suggesting that the merger of a double white dwarf system is a plausible explanation for Type Ia supernovae.</p>

<p>In addition to providing a key clue about the nature of these important events, the team's discovery shows the potential of giant astronomical surveys like the SDSS. "Twenty years ago, we decided to take three sub-spectra for each spectrum. We did that for entirely practical reasons," says Robert Lupton, the Princeton astronomer who first told Badenes about the sub-spectra. "We had no idea that would someday give us an important clue to the mystery of Type Ia supernovae. That was a great insight by Carles and Dan, and it was a bit of a lucky break for us."</p>

<p>Who knows what other "lucky breaks" the universe might have in store?</p>

