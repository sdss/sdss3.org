<p>"There are reasons to suspect that Type Ia supernovae come from the merging of a double white dwarf," Maoz says. "But the biggest question mark is&mdash;are there enough double white dwarfs out there to produce the number of Type Ia supernovae that we see?"</p>

<p>Answering that question requires counting double white dwarfs, figuring out how often they merge, and comparing that rate to the supernova rate in distant galaxies. White dwarfs are so small and faint that there is no hope of seeing them in distant galaxies, so Badenes and Maoz turned to the only place we can see them&mdash;the part of our galaxy within about a thousand light-years of the Sun. And because their goal was to count double white dwarfs in the neighborhood, they turned to the only survey that could find enough of them to count.</p>

