<p>To find a double star system, astronomers don't actually need to see both stars. Even if one of the stars is too faint, we can detect its presence by its effect on the star we do see. As the stars orbit each other at breakneck speeds, sometimes the visible star is moving towards us, sometimes away from us.</p>

<p>This back-and-forth motion can be detected in the star's spectrum (a measure of how much light the star gives off at different wavelengths), thanks to the Doppler Effect. The Doppler Effect is the reason that an ambulance siren changes in pitch from high to low as the ambulance passes. In the same way, light waves from an object that is moving towards us are shortened (blueshifted), while light waves from an object that is moving away from us are stretched (redshifted). Features in the spectrum called "spectral lines" provide landmarks to measure the shift, and hence the star's velocity.</p>

<p>But stars can move for lots of reasons, so just finding one velocity does not give enough evidence that the star has an unseen companion. To prove that, astronomers need at least two spectra. If the star's calculated velocity has changed between the time of the first and second spectrum, then astronomers know that the system is not one star, but two. Furthermore, they can deduce what kind of star the unseen companion must be. If both turn out to be white dwarfs, they can also use Einstein's General Theory of Relativity to calculate how much time remains until the two white dwarfs will merge and explode.</p>

<p>Using this technique requires at least two spectra of the white dwarf, and the SDSS had only one. So that approach wouldn't work for Badenes and Maoz. Or would it?</p>

<p>In 2008, Badenes was a postdoctoral researcher at Princeton University. Working in the same building was Robert
Lupton, one of the "founding fathers" of the Sloan Digital Sky Survey. One day over coffee, Lupton told Badenes
an important fact about SDSS data. When the SDSS telescope measures a spectrum, it is actually measuring three
sub-spectra, then adding them together to make a complete spectrum. That way, if there was something wrong with
one of the three, the survey could still use the other two. "Robert said that these spectra were there, and that
someone should really do science with them," Badenes says. "I immediately realized that those sub-spectra were the
missing piece. Now we could do exactly what we needed to do with SDSS data."</p>


