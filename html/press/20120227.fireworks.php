<p>Taking advantage of a little-known feature of the Sloan Digital Sky Survey (SDSS-III), a team of astronomers led by Carles Badenes of the University of Pittsburgh has helped to clarify the origins of an important type of exploding star&mdash;using nothing but a few thousand small, faint stars in our own cosmic backyard.</p>

<p>The astronomical fireworks the team studied are so-called "Type Ia supernovae," exploding stars so incredibly bright that we can see them even in the most distant galaxies. In a <a href="http://arxiv.org/abs/1202.5472">paper
published today on the arXiv preprint server</a>, Badenes and colleagues compared the number of these supernovae in distant galaxies to the number of binary white dwarfs in our galaxy. The rate is similar, suggesting that merging
white dwarfs are indeed a reasonable explanation for these giant explosions.</p>

<p>Type Ia supernovae, while rare, are important because of what they can tell us about the universe. "We know that all Type Ia supernovae should be about the same brightness, so if we see one that looks fainter, it must be farther away," Badenes says. "That means that if we happen to see a Type Ia supernova in a distant galaxy, we can figure out how far away that galaxy is." In fact, using Type Ia supernovae to measure galaxy distances is what led astronomers to discover that our universe is accelerating&mdash;a breakthrough discovery that was recognized with the 2011 Nobel Prize in Physics.</p>

<p>Although we know what happens during a Type Ia supernova explosion, amazingly, we don't know for sure what kinds of stars create them. "We know that there have to be two stars involved, and that one of them has to be a white dwarf," says Dan Maoz, an astronomer at Tel Aviv University in Israel and co-author of the paper released today. "But there are two possibilities for what the second star is, and we're not quite sure whether one or both possibilities is right."</p>

<p>The second star could be either a "normal" star like the Sun, or another white dwarf. If a star system contains two white dwarfs, then the white dwarfs revolve around one another at half a million miles an hour, speeding up and getting closer and closer&mdash;until one day they merge, most likely producing the fireworks of a Type Ia supernova.</p>

