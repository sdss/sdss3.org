<p>No one knows the mechanism that caused the splitting of the tidal tails. However,
scientists believe that perhaps the Sagittarius dwarf galaxy was once a part of a
binary galactic system, similar to the present day Large and Small Magellanic Clouds.
Each of these could have produced a leading and trailing tail on falling into the Milky
Way Galaxy, yielding four in all.</p>

<p>
  But co-author Geraint Lewis of Sydney University has another idea. He says,
  "Perhaps the Sagittarius dwarf galaxy has suffered an encounter with an object in the
  game of Galactic billiards. Maybe a collision with a massive clump of dark matter, or
  even another satellite galaxy, has split each of the streams into two."
</p>


<p>A final theory suggests that, just as meteors have spread into different streams
through evolution in the Solar system, debris from Sagittarius may have spread into
different streams at different points in time. Different epochs may suffer different
amounts of precession in the Galaxy, causing the split streams. "I have been running
hundreds of simulations of the disruption of the Sagittarius dwarf and this idea looks
very plausible," commented Jorge Penarrubia of the IAA, Granada, who was also
involved in the study.</p>

<p>
  Whatever the explanation, SDSS-III has provided a wealth of new information on the
  engorgement of the Sagittarius galaxy. The disruption of smaller galaxies has
  occurred many times in the history of the Milky Way and other galaxies like ours
  throughout the Universe.
</p>

<p>
  The wagging of the four tails of Sagittarius will shed new light on both the structure and formation of the Milky Way.
</p>
