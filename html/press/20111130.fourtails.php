<p>The Milky Way galaxy continues to devour its small neighboring dwarf galaxies
and the evidence is spread out across the sky.</p>

<p>A team of astronomers led by Sergey Koposov and Vasily Belokurov of the University of Cambridge
recently discovered two streams of stars in the Southern Galactic hemisphere
that were torn off the Sagittarius dwarf galaxy. This discovery came from analyzing
data from the latest Sloan Digital Sky Survey (SDSS-III) and was announced in a paper
released today (<a href="http://arxiv.org/abs/1111.7042">arXiv paper #1111.7042</a>) that connects these new streams with two previously known streams in the Northern Galactic hemisphere.</p>

