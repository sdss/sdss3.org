<p>"We have long known that when small dwarf galaxies fall into bigger galaxies,
elongated streams, or tails, of stars are pulled out of the dwarf by the enormous
tidal field," said Sergey Koposov.</p>

<p>The Sagittarius dwarf galaxy used to be one of the brightest of the Milky Way
satellites. Its disrupted remnant now lies on the other side of the Galaxy, breaking
up as it is crushed and stretched by huge tidal forces. It is so small that it has
lost half of its stars and all its gas over the last billion years.</p>

<p>Before SDSS-III, Sagittarius was known to have two tails, one in front of and
one behind the remnant. Previous SDSS imaging had already found the Sagittarius
tidal tail in the Northern Galactic sky in 2006 and revealed that one of the tails
was forked into two.</p>

<p>"That was an amazing discovery," said Vasily Belokurov, "but the remaining piece
  of the puzzle, the structure in the South, was missing until now."</p>

<p>Sergey Koposov and colleagues analyzed density maps of over 13 million
  stars in the latest release of Sloan Digital Sky Survey data,
  including the crucial coverage of the Southern Galactic sky. The new
  data show that the Sagittarius stream in the South is also split into
  two, a fatter and brighter stream alongside a thinner and fainter
  stream.  This brighter stream is more enriched with iron and other
  metals than its dimmer companion.  Because each generation of stars
  makes and distributes more metals into the next generation, the
  Cambridge astronomers concluded that the brighter stream is younger
  than the older fainter one.</p>

<p>"Sagittarius is like a beast with four tails," observed Wyn Evans, from the
Institute of Astronomy, University of Cambridge.</p>


