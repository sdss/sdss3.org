<h2>Astronomers measure the Universe's deceleration before dark energy took over</h2>

<p>For the past five billion years, the expansion of the Universe has been
speeding up, powered by the mysterious repulsive force known as "dark energy."
But thanks to a new technique for measuring the three-dimensional structure
of the distant Universe, astronomers from the Sloan Digital Sky Survey
(SDSS-III) have made the first measurement of the cosmic expansion rate
just three billion years after the Big Bang.</p>

<p>"If we think of the Universe as a roller coaster, then today we are rushing
downhill, gaining speed as we go," says Nicol&aacute;s Busca of the Laboratoire
Astroparticule et Cosmologie of the French Centre National de la Recherche
Scientifique (CNRS), one of the lead authors of the study. "Our new measurement
tells us about the time when the Universe was climbing the hill &mdash; still
being slowed by gravity."</p>

