<p>Astronomers from the Sloan Digital Sky Survey have used 140,000 distant quasars to measure the expansion rate of the Universe when it was only one-quarter of
its present age. This is the best measurement yet of the expansion rate at any epoch in the last 13 billion years.</p>

<p>The Baryon Oscillation Spectroscopic Survey (BOSS), the largest component of the third Sloan Digital Sky Survey (SDSS-III), pioneered the technique of
measuring the structure of the young Universe by using quasars to map the distribution of intergalactic hydrogen gas. Today, new BOSS observations of this
structure were presented at the April 2014 meeting of the American Physical Society in Savannah, GA.</p>



