<p>The kSZ effect can be seen in the "cosmic microwave background radiation (CMB)," the faint glow
of light given off by the universe when it was just 380,000 years old. The effect was first proposed
in 1972 by Moscow-based astrophysicists Rashid Sunyaev and Yakov Zel'dovich. The telescopes of the time
could not make measurements of the CMB - they could barely see it at all - so Sunyaev and Zel'dovich
turned to a tool they already had: their minds. Given that the CMB exists, they thought, and given
what we know about the universe, what should we expect to see?</p>

<p>Sunyaev and Zel'dovich knew that the light of the CMB has been traveling across the universe to our eyes.
Because space is big and mostly empty, most of the light from the CMB travels straight from there to
here, so we see the light just as it was nearly 14 billion years ago. But, they realized, some of the
CMB light passes through galaxy clusters on the way, and those clusters leave their mark.</p>

<p>As light from the CMB passes through a galaxy cluster, it interacts with the hot ionized gas between
galaxies. The gas is "ionized" because it is so hot that the electrons get ripped away from their atoms,
leaving them to float freely. As a microwave from the CMB passes through the cluster, every once in a
while, it hits one of these free electrons. The collision between the microwave and the electron
slightly increases the microwave's energy in the direction that the galaxy cluster is moving.</p>

<p>That means that when CMB light passes through a galaxy cluster moving toward Earth, it appears
hotter by a few millionths of a degree; when the light passes through a galaxy cluster moving away
from Earth, it appears slightly cooler. A few millionths of a degree is small even by the standards
of astronomers. Sunyaev and Zel'dovich had no way to test their prediction, and neither did anyone else.</p>

<p>The kSZ effect is so tiny that, even forty years later, measuring it is still a Herculean task.
"The kSZ signal is small because the odds of a microwave hitting an electron while passing through a
galaxy cluster are low, and the change in the microwave's energy from this collision is slight," said David
Spergel of Princeton, Hand's senior thesis advisor and an ACT collaborator. But by averaging ACT's
maps of CMB temperature for thousands of clusters, Spergel said, the kSZ signal gets stronger in
comparison to unrelated signals or the noise from measurement errors.</p>

<p>To find the CMB temperature near thousands of clusters, astronomers needed accurate maps of where
the clusters were. ACT did not have such maps, but SDSS-III did. With ACT's temperature maps and
SDSS-III's list of galaxy clusters, all that remained was to find someone with enough knowledge, time,
and patience to lead the study.</p>

<p>Enter Nick Hand. At the time, Hand was a senior at Princeton University, looking for a senior
thesis project. Working with Spergel, he began to look for ways to combine the datasets.</p>

<p>Following a suggestion from Arthur Kosowsky of the University of Pittsburgh, another ACT collaborator,
Hand marked the locations of the 7500 brightest galaxy clusters from the SDSS-III catalog on ACT's
CMB map, then measured the temperatures of the CMB at those locations. Sure enough, when he averaged
all the measurements, he found that the average temperature difference between any two galaxies showed
that they were slightly more likely to be moving towards each other than away from each other. That means
that the temperature differences between galaxies reflect the motions of galaxy clusters &mdash; just as
Sunyaev and Zel'dovich had predicted. </p>

<p>Naturally, one astronomer particularly excited about today's result is Rashid Sunyaev. He was
in his 20s when he predicted the effect that now bears his name; today, at age 69, he is the director
of the Max Planck Institute for Astrophysics in Garching, Germany. "Like any theoretical scientist
proposing an observational effect, I was dreaming for almost 40 years that it would be discovered
'in the next several years,'" Sunyaev said. "It's extremely elegant that the authors were able to
choose the most interesting groups of galaxies using the SDSS-III results."</p>

<p>Now that Sunyaev's dream has been realized, what's next? "As maps get better, we can better
detect the kSZ effect signal. One of the main advantages of the kSZ effect is that its magnitude is
independent of a cluster's distance from us," Hand said, "so we can use it to measure cosmic velocity
on large scales." In fact, the kSZ effect can measure the velocities of the clusters much more
precisely than the methods astronomers traditionally use. The ACT collaboration obtained evidence for
clusters that are several billion light-years away moving at velocities of up to 600 kilometers per
second (more than one million miles per hour).</p>

<p>The ability to measure galaxy cluster velocities so precisely could give astronomers insight into
the strength of the gravitational forces that pull them together. Chief sources of the forces are
dark energy and dark matter, so measurements of the kSZ effect could someday help to test theories
of what those mysterious substances could be. In addition, because the strength of the kSZ effect
depends on the distribution of electrons in clusters' ionized gas, the effect can also be used to
study such gas in nearby clusters. Those studies could help reveal how galaxies first formed.</p>

<p>The new discovery also holds another important lesson. Given the tiny temperature differences
that the kSZ effect produces, looking at just the SDSS-III data alone, or just the ACT data alone,
would not have been enough.</p>

<p>"This result is a great example of an important scientific discovery relying on the rich
data from large astronomy surveys," said Kosowsky. "These are big collaborations of scientists,"
added David Schlegel, an astronomer at Lawrence Berkeley National Laboratory and principal
investigator of the Baryon Oscillation Spectroscopic Survey (BOSS), one of the four components
of SDSS-III. "Working in only one of these collaborations is a full-time job. Pulling together
data from both to measure such a tiny signal was quite a feat."</p>

