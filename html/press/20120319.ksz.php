<p>Tatar astrophysicist Rashid Sunyaev, working in Moscow with his advisor Yakov Zel'dovich,
predicted in 1972 what would become known as the "kinematic Sunyaev-Zel'dovich (kSZ) effect." The tiny effect had
eluded scientists for 40 years - until Nick Hand, as an undergraduate at Princeton University -
worked with his advisor David Spergel to find it in combined data from the Atacama Cosmology Telescope
(ACT) and the Sloan Digital Sky Survey (SDSS-III).</p>


<p>Hand, now a graduate student at the University of California Berkeley, was the first of 58 authors on
a paper announcing the results, published today on the arXiv preprint server and submitted to
the journal Physical Review Letters. Now that this incredibly small record of galaxy cluster motions has
been seen, studying it further will help improve our understanding of the components of our universe and
the formation of the largest clusters of galaxies.</p>


<p>"It's an exciting confirmation of an effect first theorized 40 years ago," Hand said. "The current
generation of astronomy surveys is capable of detecting an effect that, even by astronomy standards,
is very, very slight."</p>

