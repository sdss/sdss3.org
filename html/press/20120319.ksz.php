<h2>Two telescopes, two young astronomers, one amazing discovery</h2>

<p>PRINCETON, NJ &mdash; In 1972, a young astronomer predicted that moving clusters of galaxies would leave a
subtle imprint on the cosmic microwave background radiation - but he had no way to check his prediction. Forty
years later, another young astronomer has proved him right - by combining data from two huge international
astronomy collaborations.</p>


