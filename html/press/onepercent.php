<p>The new distance measurements were presented today at the meeting of the American Astronomical Society by Harvard
University astronomer Daniel Eisenstein, the director of the Sloan Digital Sky Survey III (SDSS-III), the worldwide
collaboration of which BOSS is a part. They are detailed in a series of articles submitted to journals by the BOSS
collaboration last month, all of which are now available as online preprints.</p>

<p>"Determining distance is a fundamental challenge of astronomy," said Eisenstein. "You see something
in the sky &mdash; how far away is it? Once you know how far away it is, learning everything else
about it is suddenly much easier."</p>

<p>Throughout history, astronomers have met this challenge using many different techniques: for example,
  distances to planets in the Solar System can be measured quite accurately using radar, but for more distant
  objects, astronomers must turn to less-direct methods. Regardless of the method, every measurement has some
  uncertainty, which can be expressed as a percentage of the thing being measured &mdash; for example, if you
  measure the distance from Washington to New York (200 miles) to within 2 miles of the true value, you have
measured to an accuracy of 1%.</p>

<p>Only a few hundred stars and a few star clusters are close enough to have distances measured to
one-percent accuracy. Nearly all of these stars are only a few thousand light-years away, and all are still
within our own Milky Way galaxy. Reaching out a million times farther away, the new BOSS measurements probe
far beyond our Galaxy to map the Universe with unprecedented accuracy.</p>

<p>With these new, highly-accurate distance measurements, BOSS astronomers are making new inroads in the quest
to understand dark energy. "We don't yet understand what dark energy is," explained Eisenstein, "but we can
measure its properties. Then, we compare those values to what we expect them to be, given our current
understanding of the Universe. The better our measurements, the more we can learn."</p>

<p>Making a one-percent measurement at a distance of six billion light-years requires a completely different
technique from measurements in the Solar System or the Milky Way. BOSS, the largest of the four projects that
make up the Sloan Digital Sky Survey III (SDSS-III), was built to take advantage of this technique: measuring
the so-called "baryon acoustic oscillations" (BAOs), subtle periodic ripples in the distribution of galaxies
in the cosmos.</p>

<p>These ripples are imprints of pressure waves that moved through the early Universe, which was so hot and
dense that particles of light (photons) moved along with the protons and neutrons (known collectively as
"baryons") that today make up the nuclei of atoms. The original size of these ripples is known, and their
size today can be measured by mapping galaxies.</p>

<p>"With these galaxy measurements, nature has given us a beautiful ruler," said Ashley Ross, an astronomer
from the University of Portsmouth. "The ruler happens to be half a billion light years long, so we can use it
to measure distances precisely, even from very far away."</p>

