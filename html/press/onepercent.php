<p>Researchers from the Baryon Oscillation Spectroscopic Survey (BOSS) today announced that they have measured
the distances to galaxies more than six billion light-years away to an unprecedented accuracy of just one percent.
Their measurements place new constraints on the properties of the mysterious "dark energy" thought to permeate
empty space, which causes the expansion of the Universe to accelerate.</p>

<p>"There are not many things in our daily lives that we know to one-percent accuracy," said David Schlegel, a
physicist at Lawrence Berkeley National Laboratory (LBNL) and the principal investigator of BOSS. "I now know
the size of the Universe better than I know the size of my house."</p>





