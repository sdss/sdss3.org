<p>Making these measurements required astronomers to map the locations of 1.2 million galaxies. BOSS
uses a specialized instrument that can make detailed measurements of 1000 galaxies at a time. "On a
clear night when everything goes perfectly, we can add more than 8000 galaxies and quasars to the map,"
said Kaike Pan, who leads the team of observers at the SDSS-III's Sloan Foundation 2.5-meter Telescope
at Apache Point Observatory in New Mexico.</p>

<p>The BOSS team presented preliminary BAO measurements from its early galaxy maps a year ago, but the new
analysis covers an area more than twice as large, and thus provides a much more precise measurement. The new
dataset also includes the first BAO measurements from a sample of nearby galaxies. "Making these measurements
at two different distances allows us to see how the expansion of the Universe has changed over time, which
will help us understand why it is accelerating," explained University of Portsmouth astronomer Rita Tojeiro,
who co-chairs the BOSS galaxy clustering working group along with Jeremy Tinker of New York University.</p>

<p>Mariana Vargas-Maga&ntilde;a, a postdoctoral researcher at Carnegie Mellon University, led a companion
paper that investigates many of the subtle effects that could influence the BOSS measurement. "When you're
trying to reach one percent, you have to be paranoid about everything that could go even slightly wrong,"
said Vargas-Maga&ntilde;a &mdash; for example, slight differences in how galaxies were identified could
have thrown off the entire measurement of their distribution, so different parts of the sky had to be checked
carefully. "Fortunately," Vargas-Maga&ntilde;a said, "there are plenty of careful people on our team to check
our assumptions. By the time all of them are satisfied, we are sure we didn't miss anything."</p>

<p>For now, the BOSS measurements appear consistent with a form of dark energy that stays constant through the
history of the Universe. This "cosmological constant" is one of just six numbers needed to make a model that
matches the shape and large-scale structure of the Universe. Schlegel likens this six-number model to a pane of
glass, which is pinned in place by bolts that represent different measurements of the history of the Universe.
"BOSS now has one of the tightest of those bolts, and we just gave it another half-turn," said Schlegel. "Each
time you ratchet up the tension and the glass doesn't break, that's a success of the model."</p>



