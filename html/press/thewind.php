<p>
Many quasars show evidence of these winds in their spectra &mdash; measurements of
the amount of light that the quasar gives off at different wavelengths. Just
outside the center of the quasar are clouds of hot gas flowing away from the
central black hole.  As light from deeper in the quasar passes through these
clouds on its way to Earth, some of the light gets absorbed at particular
wavelengths corresponding to the elements in the clouds.
</p>

<p>
As gas clouds are accelerated to high speeds by the quasar, the Doppler
effect spreads the absorption over a broad range of wavelengths, leading to a
wide valley visible in the spectrum. The width of this "broad absorption line
(BAL)" measures the speed of the quasar's wind. Quasars whose spectra show
such broad absorption lines are known as "BAL quasars."
</p>

<p>
But the hearts of quasars are chaotic, messy places. Quasar winds blow at
thousands of miles per second, and the disk around the central black hole is
rotating at speeds that approach the speed of light. All this adds up to an
environment that can change quickly.
</p>

<p>
Previous studies had found a few examples of quasars whose broad absorption
lines seemed to have disappeared between one observation and the next. But
these quasars had been found one at a time, and largely by chance &mdash; no one
had ever done a systematic search for them. Undertaking such a search would
require measuring spectra for hundreds of quasars, spanning several years.
</p>

<p>
Enter the Sloan Digital Sky Survey (SDSS). Since 1998, SDSS has been regularly
measuring spectra of quasars. Over the past three years, as part of SDSS-III's
Baryon Oscillation Spectroscopic Survey (BOSS), the survey has been specifically
seeking out repeated spectra of BAL quasars through a program proposed by Brandt
and colleagues.
</p>

<p>
Their persistence paid off &mdash; the research team gathered a sample
of 582 BAL quasars, each of which had repeat observations over a period of
between one and nine years &ndash; a sample about 20 times larger than any
that had been previously assembled. The team then began to search for changes,
and were quickly rewarded. In 19 of the quasars, the broad absorption lines
had disappeared.
</p>

<p>
What's going on here? There are several possible explanations, but the
simplest is that, in these quasars, gas clouds that we had seen previously
are literally "gone with the wind" &mdash;the rotation of the quasar's disk
and wind have carried the clouds out of the line-of-sight between us and
the quasar.
</p>

<p>
And because the sample of quasars is so large, and had been gathered in such
a systematic manner, the team can go beyond simply identifying disappearing
gas clouds. "We can quantify this phenomenon," says Filiz Ak.
</p>

<p>
Finding nineteen such quasars out of 582 total indicates that about three
percent of quasars show disappearing gas clouds over a three-year span, which
in turn suggests that a typical quasar cloud spends about a century along our
line of sight. "Since the universe is 14 billion years old, we're used to
astronomical phenomena lasting a very long time," says Pat Hall of York
University in Toronto, another team member. "It's fascinating to discover
something that changes within a human lifetime."
</p>

<p>
Now, as other astronomers come up with models of quasar winds, their models
will need to explain this 100-year timescale. As theorists begin to consider
the results, the team continues to analyze their sample of quasars &mdash; more
results are coming soon. "This is really exciting for me," Filiz Ak says.
"I'm sitting at my desk, discovering the nature of the most powerful winds in
the Universe."
</p>



