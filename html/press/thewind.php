<p>
Quasars are powered by gas falling into supermassive black holes at the centers
of galaxies. As the gas falls into the black hole, it heats up and gives off
light. The gravitational force from the black hole is so strong, and is
pulling so much gas, that the hot gas glows brighter than the entire surrounding
galaxy.
</p>


<p>
But with so much going on in such a small space, not all the gas is able to find
its way into the black hole. Much of it instead escapes, carried along by strong
winds blowing out from the center of the quasar.
</p>

<p>
"These winds blow at thousands of miles per second, far faster than any winds
we see on Earth," says Niel Brandt, a professor at Penn State and Filiz Ak's Ph.D.
advisor. "The winds are important because we know that they play an important
role in regulating the quasar's central black hole, as well as star formation
in the surrounding galaxy."
</p>


