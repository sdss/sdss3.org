<p>The case of the missing quasar gas clouds has been solved by a worldwide team
of astronomers, and the answer is blowin' in the wind.</p>

<p>Astronomers Nurten Filiz Ak and Niel Brandt of the Pennsylvania State
University led the team, which announced their results in a paper published
in today's issue of <i>The Astrophysical Journal</i>. The paper describes 19
distant quasars in which giant clouds of gas seemed to disappear in just a few years.</p>

<p>"We know that many quasars have structures of fast-moving gas caught up in
'quasar winds,' and now we know that those structures can regularly disappear
from view," says Filiz Ak, a graduate student at Penn State and lead author
of the paper. "But why is this happening?"</p>


