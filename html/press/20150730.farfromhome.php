<p>When it comes to our galaxy, home is where the star is.</p>

<p>Scientists with the Sloan Digital Sky Survey (SDSS) have created a new map 
of the Milky Way and determined that 30 percent of stars have dramatically 
changed their orbits. This discovery, published in yesterday's issue of 
<em>The Astrophysical Journal</em>, brings a new understanding of how stars 
are formed, and how they travel throughout our galaxy.</p>


