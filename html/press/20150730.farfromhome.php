<p>"While on average the stars in the outer disk of the Milky Way have less 
heavy element enrichment, there is a fraction of stars in the outer disk that 
have heavier element abundances that are more typical of stars in the inner 
disk," says Jo Bovy of the Institute for Advanced Study and the University of 
Toronto, another key member of the research team.</p>

<p>When the team looked at the pattern of element abundances in detail, they found 
that much of the data could be explained by a model in which stars migrate into 
new orbits around the Galactic Center, moving nearer or farther with time. 
These random in-and-out motions are referred to as "migration," and are likely 
caused by irregularities in the Galactic disk, such as the Milky Way's famous 
spiral arms. Evidence of stellar migration had previously been seen in stars 
near the Sun, but the new study is the first clear evidence that migration occurs 
for stars throughout the Galaxy.</p>

<p>Future studies by astronomers using data from SDSS promise even more new 
discoveries. "These latest results take advantage of only a small fraction of 
the available APOGEE data," says Steven Majewski, the Principal Investigator 
of APOGEE. "Once we unlock the full information content of APOGEE, we will 
understand the chemistry and shape of our galaxy much more clearly."</p>



