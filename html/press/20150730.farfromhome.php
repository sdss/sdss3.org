<p>"In our modern world, many people move far away from their birthplaces, 
sometimes halfway around the world," says Michael Hayden of New Mexico 
State University (NMSU), the lead author of the new study. "Now we're 
finding the same is true of stars in our galaxy &mdash; about 30 percent 
of the stars in our Galaxy have traveled a long way from the orbits in 
which they were born."</p>

<p>To build a map of the Milky Way, the scientists used the SDSS Apache 
Point Observatory Galactic Evolution Explorer (APOGEE) spectrograph to 
observe 146,000 stars during a 3-year campaign.</p>

<p>The key to creating and interpreting this map is measuring the elements 
in the atmosphere of each star. "From the chemical composition of a star, 
we can learn its ancestry and life history," Hayden says.</p>

<p>The chemical information comes from spectra, which are detailed measurements 
of how much light the star gives off at different wavelengths. Spectra show 
prominent lines that correspond to elements and molecules present. Reading 
the spectral lines of a star can tell astronomers what the star is made of.</p>

<p>"Stellar spectra show us that the chemical makeup of our galaxy is constantly 
changing," says Jon Holtzman, an astronomer at NMSU who was involved in the study. 
"Stars create heavier elements in their cores, and when the stars die, those 
heavier elements go back into the gas from which the next stars form."</p>

<p>As a result of this process of "chemical enrichment," each generation of 
stars has a higher percentage of heavier elements than the previous generation 
did. In some regions of the Galaxy, star formation has proceeded more vigorously 
than in other regions -- and in these more vigorous regions, more generations 
of stars have formed. Thus, the average amount of heavier elements in stars 
varies across different parts of the Galaxy.  Astronomers can use the amount 
of heavy elements in a star to determine what part of the Galaxy the star was 
born in.</p>

<p>Hayden and colleagues used APOGEE data to map the relative amounts of 15 
separate elements, including carbon, silicon, and iron, for stars all over the 
Galaxy.</p>

<p>What they found surprised them &mdash; up to 30 percent of stars had 
compositions indicating that they were formed in parts of the Galaxy far from 
their current positions.</p>


