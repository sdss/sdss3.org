<p>Today, astronomers with the Sloan Digital Sky Survey III (SDSS-III) released a new online
public data set featuring 60,000 stars that are helping to tell the story of how our Milky Way galaxy
formed.</p>

<p>The highlight of today's "Data Release 10" is a new set of high-resolution stellar spectra &mdash;
measurements of the amount of light given off by a star at each wavelength &mdash; using infrared light,
invisible to human eyes but able to penetrate the veil of dust that obscures the center of the Galaxy.</p>



