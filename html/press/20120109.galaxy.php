<p>The Apache Point Observatory Galactic Evolution Experiment (APOGEE)
spectrograph is the newest instrument deployed by the Sloan Digital Sky
Survey III (SDSS-III).  The revolutionary capabilities of this high-resolution
infrared spectrograph were presented today by Steven Majewski and John Wilson
of the University of Virginia at the 219th meeting of the American Astronomical
Society in Austin, TX.</p>

<p>Over the next three years APOGEE's initial census of the chemical
constitution and motions of more than 100,000 stars spread all over the
Milky Way will bring together data on stars with ages spanning nearly the
full age of the universe, including some of the earliest ever created. These
fossils of earlier times will help astronomers piece together how the Milky Way
grew by devouring smaller galaxies, whose stellar entrails may still be
identified through their particular chemical compositions and motions within
the Galaxy. </p>

<p>In just the first half year of operation, the APOGEE project has observed
with exquisite spectral resolution 32,000 stars throughout the Milky Way &mdash;
more than four times as many stars as have ever previously been observed in
such detail at near-infrared wavelengths by all of the world's telescopes
combined.  Observations in the near infrared are especially critical for
penetrating the veil of dust that obscures most of the stars in our galaxy
from view, while high spectral resolution allows for precise measurements
of the chemical composition and motions of different types of stars
throughout our galaxy.</p>

<p>&quot;APOGEE is enabled by several novel technologies, including a
sophisticated fiber optic system capable of simultaneously channeling the
light of hundreds of individual stars from the telescope, one of the
largest-ever deployed holographic dispersing elements to spread the
light from each star into an infrared rainbow, and a 250-pound (110 kg),
six-element camera with pure silicon lenses 16 inches (400 mm) in diameter
used to record the vast number of stellar rainbows being created at once,&quot;
said Wilson, who led the hardware team in the development of the
state-of-the-art instrument.</p>

<p>By the end of its first three-year general survey of the Milky Way in
mid-2014, SDSS-III scientists will have used the APOGEE spectrograph to
derive precise Doppler motions for stars distributed throughout the Milky Way,
and will, for each of these stars, measure the abundances of fifteen chemical
elements, including carbon, nitrogen and oxygen &mdash; key building-blocks for
life.  One of the biggest advances of APOGEE over previous infrared
spectrographs is its ability to gather these precise data on as many as 300
stars at once.</p>

