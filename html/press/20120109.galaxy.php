<p>&quot;The combination of infrared sensitivity and a 300-target multiplexing
capability will make it possible for APOGEE to create the first-ever systematic
and comprehensive probe of stars in every part of our galaxy.  From this census
of our Milky Way we expect many new findings about its structure, dynamics and
constituent stellar populations ... and to do it hundreds of times faster than
would be possible with conventional, one-star-at-a-time instruments,&quot; said
Majewski, the principal investigator for the APOGEE project.</p>

<p>Our galaxy has three major components: the disk, where our Sun lies, a much
larger but more diffusely populated spherical &quot;halo&quot; surrounding the
disk, and a central bulge, densely populated with stars and elongated into a
bar-like shape.  Observing the bulge and much of our Milky Way's disk beyond
the Sun's local neighborhood is challenging because huge swathes of
intervening dust block much of the light at the traditional, visual wavelengths
used for this type of work.  Previous Milky Way surveys (like SEGUE, APOGEE's
sister survey in SDSS-III, which operates at optical wavelengths) have
typically avoided these dustier regions of the Galaxy.</p>

<p>But APOGEE's near-infrared view already is punching through the dust to reveal
previously unseen phenomena in the heart of the Milky Way and beyond &mdash;
to the normally hidden, far-side of the disk.</p>

<p>&quot;This capability has already enabled us to discover a previously
unseen pattern of stellar motions likely influenced by the Milky Way's
central bar,&quot; said David Nidever, who will present some of the first
scientific results from the APOGEE survey at the meeting.  &quot;I can't
wait to dig into the full set of APOGEE data, which will allow us to
determine the properties of the bar and the far side of the Galaxy in
significantly more detail than has been possible before.&quot;</p>


