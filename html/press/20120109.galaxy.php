<p>By spreading the infrared light from stars into infrared rainbows,
APOGEE's spectroscopic observations allow SDSS-III astronomers to sense
stellar motions through the Doppler effect, which shifts a star's spectral
features by an amount corresponding to how quickly the star is moving toward
or away from us.  Because of the high degree with which APOGEE can spread
starlight, the instrument can detect relatively small differences in motion.
With such sensitivity APOGEE astronomers can identify groups of stars having
highly correlated motions, a phenomenon that is often an indication of a
common origin.  Such data make it possible to discriminate stars born and
traveling together in clusters, or that were pulled out of the same star
system cannibalized by the Milky Way.</p>

<p>The APOGEE instrument was assembled at the University of Virginia and
delivered to the Sloan Telescope in New Mexico last April.  Within weeks it
had recorded its &quot;first-light&quot; observations of a field of star
clusters in the direction of the constellation Cygnus (the Swan, or Northern
Cross).  Star clusters are particularly valuable targets for Galactic
astronomers because the distances and ages of these star systems can be
estimated with much greater reliability than those of individual stars.
Unfortunately, the vast majority of star clusters are commingled with the
dust and myriad other, loosely distributed stars in our galaxy's disk.
This mingling makes the clusters not just difficult to study but also a
challenge to identify reliably.  With its first observations, APOGEE proved
that it could not only efficiently identify those stars that are members of
clusters but in addition provide rich amounts of other information about the
motions, chemical compositions, and ages of these star systems.</p>

<p>&quot;These first dozen or so clusters in the 'first-light' field are
just the tip of the iceberg for APOGEE.  The full APOGEE survey will contain
data on many hundreds of clusters, vital tools for exploring the chemical and
dynamical history of our Galaxy,&quot; remarked Peter Frinchaboy
(Texas Christian University), who is demonstrating findings from the
APOGEE &quot;first-light&quot; spectra at the meeting.</p>

<p>Because each chemical element or molecule absorbs a characteristic set of
wavelengths of light, the lines observed in a stellar spectrum reveal the
chemical composition of a star.  Some of the most anticipated results from
APOGEE will come from exploring how the chemical compositions of stars change
with their ages and locations in the Galaxy.  These variations in composition
come about because stars use nuclear fusion in their cores to join lighter
atoms into heavier atoms.  In the process of living and dying, some stars
deposit those heavier atoms back into the interstellar gas clouds used to
create subsequent generations of stars in the Galaxy.  APOGEE's huge chemical
database on so many stars will yield vital clues to how our galaxy formed in
an early universe of primarily hydrogen and helium and subsequently evolved
through many generations of stars to its present level of heavy element
enrichment &mdash; a level sufficient to allow solar systems like our own to
form with all of the necessary ingredients to produce Earth-like planets and,
ultimately, life.</p>
