<p>Principal Investigator: Steven Majewski (UVa)</p>

<p>The <b>APO Galaxy Evolution Experiment 2 (APOGEE-2)</b> will
    explore the formation history of the Milky Way using the
    "archeological" record provided by hundreds of thousands of its
    individual stars. The details of the assembly and growth of the
    Galaxy from its progenitors are preserved in the patterns seen
    today in the motions of stars and the abundances of chemical
    elements that they contain. We will map these patterns using
    observations from the Apache Point site and from the 2.5m du Pont
    Telescope at Las Campanas Observatory in Chile, obtaining a
    complete view of our Galaxy's history. Simultaneously, in
    cooperation with NASA's planet-finding mission, Kepler, APOGEE
    will measure the abundance of carbon, oxygen, nitrogen and iron in
    planet-hosting stars, to study the role that these elements play
    in the formation of terrestrial planets. </p>

<h3>Key Science Questions</h3>
<ul>
<li>What is the history of star-formation and chemical enrichment of
the Milky Way?</li>
<li>What are the dynamics of the disk, bulge and halo of the Milky
Way?</li>
<li>What is the age distribution of stars in the Milky Way?</li>
<li>Are certain properties of stars associated with whether or 
not they have planets?</li>
</ul>

