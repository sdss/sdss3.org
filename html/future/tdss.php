<p>Principal Investigators: Paul Green (SAO) and Scott Anderson (UW).</p>

<p>The <b>Time-Domain Spectroscopic Survey (TDSS)</b> will select
    time-variable targets for spectroscopic follow-up. These targets
    will include quasars and several classes of variable stars, some
    of which may reveal previously unidentified phenomena.  By
    spectroscopically identifying the physical nature of 100,000
    detected variables, TDSS will greatly enhance the power of these
    existing data sets to illuminate the physics of stars and quasars.
    Future photometric surveys (such as the Large Synoptic Survey
    Telescope) will leverage TDSS observations to classify their
    detections more robustly. </p>
    
<p>TDSS will additionally devote a number of fiber to "few-epoch
spectroscopy" to study spectroscopic variability of special classes,
such as double-peaked emission line quasars, broad-absorption line
quasars,  and dwarf carbon stars. </p>

<p>The combination of TDSS with the <a
href="future/eboss.php">eBOSS</a> large-scale structure sample of
quasars, and the <a href="future/spiders.php">SPIDERS</a> X-ray
selected AGN sample, will create a unique window into the full
population of quasars at all epochs to redshift z = 3.</p>

