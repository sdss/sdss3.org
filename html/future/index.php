<p>After the end of SDSS-III in June 2014, the Sloan Foundation 2.5m
	Telescope at Apache Point Observatory will remain the world's
	premier wide-field spectroscopic facility. The Astrophysical
	Research Consortium (ARC) has been planning the next-generation SDSS
	project taking advantage of this unique resource from 2014 through
	2020. Beginning in mid-2010, ARC sought proposals for use of the
	telescope.  In November 2011, ARC chose the following programs for
	the SDSS-IV project:</p>

<ul>
<li> <a href="future/apogee2.php"><b>APOGEE-2</b></a>: A stellar survey of the Milky Way, with two
	major components:
  <ul>
   <li> A northern survey using the bright time at APO.</li>
   <li> A southern survey using the 2.5m du Pont Telescope at Las
   Campanas.</li>
  </ul>
 </li>
<li> <a href="future/eboss.php"><b>eBOSS</b></a>: A cosmological survey of quasars and galaxies, also
	encompassing two major subprograms:
  <ul>
   <li><a href="future/tdss.php"><b>TDSS</b></a>: a spectroscopic survey of variable sources.</li>
   <li><a href="future/spiders.php"><b>SPIDERS</b></a>: a survey of X-ray emitting sources from
   eROSITA.</li>
  </ul></li>
<li> <a href="future/manga.php"><b>MaNGA</b></a>: A survey to obtain resolved spectroscopy for 10,000 nearby galaxies.</li>
</ul>

<p>In October 2012, we reached an important milestone: the Sloan
	Foundation approved a key piece of our funding. We are currently
	developing the survey plans, building the necessary new
	infrastructure for this project, and seeking partners for this
	project. Like previous generations of the SDSS, we expect
	participation and contributions from a broad set of institutions
	world-wide.</p>

