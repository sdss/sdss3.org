   <table class="common">
   <tr><th>MaNGA Instrumentation</th></tr>
   <tr><td>Buffered fibers with 120 micron (2'') core diameters</td></tr>
   <tr><td>Close-packed hexagonal fibers IFUs, 54% live-core fill factor</td></tr>
   <tr><td>IFU size from 19 to 127 fibers, diameters from 12'' to 32''</td></tr>
   <tr><td>IFU complement per plate: 2x19; 4x37; 4x61; 2x91; 5x127</td></tr>
   <tr><td>92 IFU-associated sky fibers</td></tr>
   <tr><td>12 7-fiber "mini-bundles" for spectrophotometric calibration</td></tr>
   <tr><td>Total number of fibers: 1423</td></tr>
   </table>


