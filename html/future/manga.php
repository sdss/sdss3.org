<h2>MaNGA Technical Details</h2>

<table class="common">
<tr><th>MaNGA at a glance</th></tr>
<tr><td>Dark-time observations</td></tr>
<tr><td>Fall 2014 - Spring 2020 </td></tr>
<tr><td>17 IFUs per 7 deg<sup>2</sup> plate </td></tr>
<tr><td>Wavelength: 360-1000 nm, resolution R~2000</td></tr>
   <tr><td>10,000 galaxies across ~4000 deg<sup>2</sup>, redshift z~0.03</td></tr>
<tr><td>3-hour exposures with dithering</td></tr>
<tr><td>Spatial sampling of 1-2 kpc</td></tr>
<tr><td>Per-fiber S/N=5-10 at 1.5 Re</td></tr>
</table>

<h3>Sample Selection</h3>

   <p>Galaxies will be selected from the SDSS Main Galaxy Legacy Area, with selection cuts applied to only redshift and a color-based stellar mass estimate.</o>

   <table class="common">
   <tr><th>The MaNGA Sample</th></tr>
   <tr><td>Flat stellar mass distribution with M > 10<sup>9</sup> Msun</td></tr>
   <tr><td>Smallest galaxy diameter sampled by at least 5 spatial bins </td></tr>
   <tr><td>Primary Sample: 60%, spatial coverage to 1.5 Re </td></tr>
   <tr><td>Secondary Sample: 30%, spatial coverage to 2.5 Re </td></tr>
   <tr><td>Tertiary Sample: 10%, volume-limited shell at the Coma distance </td></tr>
   <tr><td>No size or inclination cuts </td></tr>
   </table>


