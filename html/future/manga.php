<p>Principal Investigator: Kevin Bundy (Kavli IPMU)</p>

  <p><b>Mapping Nearby Galaxies at APO (MaNGA):</b> Unlike previous surveys which measured light only at the centers of target galaxies, MaNGA will bundle sets of optical fibers into tightly-packed arrays, enabling spectral measurements across the face of each of ~10,000 nearby galaxies.</p>

<p> MaNGA's goal is to understand the "life cycle" of present day galaxies from imprinted clues of their birth
and assembly, through their ongoing growth via star formation and merging, to their death from
quenching at late times.</p>

<h3>Key Science Questions</h3>
<ul>
<li>How does gas accretion drive the growth of galaxies?</li>
<li>What are the roles of stellar accretion, major mergers, and instabilities in forming galactic bulges and ellipticals?</li>
<li>What quences star formation? What external forces affect star formation in groups and clusters?</li>
<li>How was angular momentum distributed among baryonic and non-baryonic components as the galaxy formed?</li>
<li>How do various mass components assemble and influence one another?</li>
</ul>

<p>To answer these questions, MaNGA will provide two-dimensional <i>maps of stellar velocity and velocity dispersion, mean stellar age and star formation
  history, stellar metallicity, element abundance ratio, stellar mass surface density, ionized gas velocity, ionized gas
  metallicity, star formation rate and dust extinction</i> for a statistically powerful sample.  The galaxies are selected to span a
stellar mass interval of nearly 3 orders of magnitude. No cuts are made on color, morphology or environment, so the sample is
fully representative of the local galaxy population.</p>


<p>Just as
tree-ring dating yields information about climate on Earth hundreds of
years into the past, MaNGA's observations of the dynamical structures
and composition of galaxies will help unravel their evolutionary histories.</p>

