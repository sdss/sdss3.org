<p>The <b>Extended Baryon Oscillation Spectroscopic Survey (eBOSS)</b>
    will precisely measure the expansion history of the Universe
    throughout eighty percent of cosmic history, back to when the
    Universe was less than three billion years old, and improve
    constraints on the nature of dark energy. "Dark energy" refers the
    observed phenomenon that the expansion of the Universe is
    currently accelerating, which is most mysterious experimental
    result in modern physics. </p>

<p>eBOSS concentrates its efforts on the observation of galaxies and
    in particular quasars, in a range of distances (redshifts)
    currently left completely unexplored by other three-dimensional
    maps of large-scale structure in the Universe. In filling this
    gap, eBOSS will create the largest volume survey of the Universe
    to date. The figure to the right shows the region that will be
    newly mapped by the eBOSS project. This region corresponds to the
    epoch when the Universe was transitioning from deceleration due to
    the effects of gravity, to the current epoch of acceleration.</p>

<p>The combination of eBOSS with the <a
href="future/spiders.php">SPIDERS</a> X-ray selected sample of
quasars, and the <a href="future/tdss.php">TDSS</a> variability
selected AGN sample, will create a unique window into the full
population of quasars at all epochs to redshift z = 3.</p>

<h3>Key Science Questions</h3>
<ul>
<li>How does the transition from deceleration to acceleration occur?
Is it consistent with existing theories of dark energy?</li> 
<li>How does structure grow during this epoch? Are there signs of
violations of the general relativistic theory of gravity, which could
be related to the acceleration? </li>
<li>Can we detect anomalies in the very largest scale clustering that
could tell us about the earliest moments in the Universe's
history?</li>
<li>Can we detect the effects of cosmic neutrinos, and thus pin down
the neutrino mass scale? </li>
<li>What is the evolution of bright quasars of all luminosities out to
redshift z = 3?</li>
</ul>

